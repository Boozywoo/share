    <p>180. При организованных автомобильных перевозках групп детей автобусами в нерегулярном сообщении обеспечение безопасности движения возлагается на автомобильного перевозчика, а безопасного поведения детей - на заказчика и назначенных им сопровождающих.</p>

    <p>181. Сопровождение автобусов, используемых для выполнения организованных автомобильных перевозок групп детей, и инструктаж водителей этих автобусов проводятся соответствующими подразделениями Государственной автомобильной инспекции.</p>

    <p>182. Отбор лиц для сопровождения детей, а также инструктаж этих лиц осуществляются заказчиком.</p>

