    <p align="center">Глава 6</p>

    <p align="center">ГОРОДСКИЕ И ПРИГОРОДНЫЕ АВТОМОБИЛЬНЫЕ ПЕРЕВОЗКИ ПАССАЖИРОВ</p>

    <p align="center">В РЕГУЛЯРНОМ СООБЩЕНИИ</p>

    <p></p>

    <p>79. Городские автомобильные перевозки пассажиров могут быть организованы в обычном регулярном, скоростном регулярном и экспрессном регулярном сообщениях.</p>

    <p>80. Городские автомобильные перевозки пассажиров в обычном регулярном сообщении должны осуществляться автобусами между основными пассажирообразующими пунктами города и иметь промежуточные остановочные пункты, как правило, с расстоянием между ними при многоэтажной застройке 350 - 800 м, при малоэтажной - 500 - 1000 м.</p>

    <p>81. При городских автомобильных перевозках пассажиров в скоростном регулярном сообщении на маршруте должно быть не более чем в два раза меньше промежуточных остановочных пунктов по сравнению с количеством остановочных пунктов на этом маршруте автомобильной перевозки пассажиров в обычном регулярном сообщении.</p>

    <p>82. При городских автомобильных перевозках пассажиров в экспрессном регулярном сообщении автобусами (кроме перевозок автобусами категории М2) на маршруте должно быть не менее чем в два раза меньше промежуточных остановочных пунктов по сравнению с количеством остановочных пунктов на этом маршруте автомобильной перевозки пассажиров в обычном регулярном сообщении или расстояние между остановочными пунктами более 2 км.</p>

    <p>83. При городских автомобильных перевозках пассажиров в экспрессном регулярном сообщении автобусами категории М2 остановки автобусов на промежуточных остановочных пунктах могут производиться только при необходимости высадки и посадки пассажиров.</p>

    <p>84. Пригородные автомобильные перевозки пассажиров могут быть организованы в обычном регулярном, скоростном регулярном и экспрессном регулярном сообщениях.</p>

    <p>85. Пригородные автомобильные перевозки пассажиров в обычном регулярном сообщении должны обеспечивать потребность пассажиров в поездках между пассажирообразующими пунктами (включая дачные поселки, зоны отдыха, поселки городского типа, центры сельских исполнительных комитетов), и на маршрутах таких перевозок расстояния между остановочными пунктами должны быть не более 6000 м (при наличии жилых строений) и не менее 1500 м (для вновь открываемых маршрутов).</p>

    <p>86. Пригородные автомобильные перевозки пассажиров в скоростном регулярном сообщении должны организовываться между основными пассажирообразующими пунктами, и на маршрутах этих перевозок должно быть меньше остановочных пунктов по сравнению с количеством остановочных пунктов на маршруте автомобильной перевозки пассажиров в обычном регулярном сообщении.</p>

    <p>87. При пригородных автомобильных перевозках пассажиров в экспрессном регулярном сообщении (кроме перевозок автобусами категории М2) должно быть не менее чем в два раза меньше промежуточных остановочных пунктов по сравнению с количеством остановочных пунктов на маршруте автомобильной перевозки пассажиров в обычном регулярном сообщении или расстояние между остановочными пунктами более 12 км.</p>

    <p>88. При пригородных автомобильных перевозках пассажиров в регулярном экспрессном сообщении автобусами категории М2 автобус останавливается на промежуточных остановочных пунктах только при необходимости высадки и посадки пассажиров.</p>

    <p>89. Заказчик или оператор городских и пригородных автомобильных перевозок пассажиров в регулярном сообщении имеет право:</p>

    <p>изучать спрос населения на автомобильные перевозки пассажиров в регулярном сообщении;</p>

    <p>устанавливать число и места остановочных пунктов маршрутов перевозок пассажиров в регулярном сообщении;</p>

    <p>вносить изменения в расписание или интервалы движения автобусов по согласованию с автомобильным перевозчиком, выполняющим перевозки пассажиров по маршруту в регулярном сообщении;</p>

    <p>контролировать выполнение автомобильными перевозчиками условий договора об организации перевозок пассажиров в регулярном сообщении;</p>

    <p>управлять движением автобусов по маршрутам;</p>

    <p>расторгнуть в одностороннем порядке с автомобильным перевозчиком договор об организации автомобильных перевозок пассажиров в регулярном сообщении в соответствии с условиями этого договора, а также при грубых нарушениях перевозчиком законодательства о лицензировании или установленных лицензионных требований и условий осуществления лицензируемой деятельности.</p>

    <p>90. Заказчик или оператор городских и пригородных автомобильных перевозок пассажиров в регулярном сообщении обязан:</p>

    <p>сформировать маршруты автомобильных перевозок пассажиров, по которым необходимо организовать движение автобусов в регулярном сообщении для удовлетворения спроса населения на автомобильные перевозки пассажиров в регулярном сообщении;</p>

    <p>проводить конкурс на право выполнения автомобильных перевозок пассажиров в регулярном сообщении в случаях, установленных законодательством;</p>

    <p>контролировать полноту оплаты проезда пассажирами;</p>

    <p>обеспечить наличие паспорта маршрута на каждый маршрут автомобильных перевозок пассажиров в регулярном сообщении;</p>

    <p>для выполнения перевозок пассажиров в регулярном сообщении заключить с автомобильными перевозчиками договор об организации автомобильных перевозок пассажиров в регулярном сообщении;</p>

    <p>контролировать качество и объем выполняемых автомобильными перевозчиками услуг;</p>

    <p>обеспечивать наличие расписания движения автобусов на остановочных пунктах маршрута автомобильных перевозок пассажиров в регулярном сообщении;</p>

    <p>рассматривать обращения граждан, индивидуальных предпринимателей и юридических лиц по вопросам организации и выполнения перевозок пассажиров в сроки, установленные законодательством Республики Беларусь.</p>

    <p>91. При городских автомобильных перевозках пассажиров в регулярном сообщении могут применяться как билеты одноразового пользования - на одну поездку, так и многоразового - на декаду, месяц и другие периоды времени.</p>

    <p>92. Билеты одноразового и многоразового пользования на городские перевозки пассажиров могут предназначаться для проезда только в автобусах или в автобусах и других видах городского пассажирского транспорта.</p>

    <p>93. На бланке билета должны быть указаны:</p>

    <p>название города, заказчика, оператора или автомобильного перевозчика;</p>

    <p>назначение, номер, серия и стоимость;</p>

    <p>срок действия (число поездок) и перечень видов транспорта, на которые распространяется его действие, и при необходимости краткая информация об условиях перевозок.</p>

    <p>94. Реализация билетов на городские автомобильные перевозки пассажиров в регулярном сообщении производится в пунктах продажи билетов и в салонах автобусов. Билет обмену и возврату не подлежит.</p>

    <p>95. Билеты на проезд в транспортных средствах при пригородных автомобильных перевозках пассажиров в регулярном сообщении должны продаваться в билетных кассах, расположенных на пассажирских терминалах, остановочных пунктах маршрутов и в других местах, а при отсутствии билетных касс - уполномоченным на то водителем автобуса или кондуктором с использованием кассовых суммирующих аппаратов транспорта или бланочных билетов установленной формы. Продажа бланочных билетов одноразового пользования отмечается в билетно-учетном листе.</p>

    <p>96. Билет одноразового пользования при пригородной автомобильной перевозке пассажиров в регулярном сообщении должен содержать наименование заказчика, оператора или автомобильного перевозчика, серию (на бланке билета) и номер, наименования начального и конечного остановочных пунктов маршрута поездки, маршрут движения автобуса, дату и время начала поездки, номер места (при продаже билета в автобусе - необязательно), стоимость проезда, дату и время выдачи билета. На бланочных билетах одноразового пользования могут не указываться маршрут движения автобуса, дата и время выдачи билета.</p>

    <p>97. Пригородные автомобильные перевозки пассажиров в регулярном сообщении могут выполняться по билетам многоразового пользования - на декаду, месяц и другие периоды. Билеты многоразового пользования могут быть оформлены на конкретное лицо.</p>

    <p>98. В билете многоразового пользования должны быть указаны заказчик, оператор или автомобильный перевозчик, назначение билета, номер, серия и стоимость, маршрут (часть маршрута, маршруты, пункт назначения, территория) и период действия (число поездок), фамилия, имя и отчество пассажира (на билете на конкретное лицо), а также при необходимости - краткая информация об условиях автомобильных перевозок.</p>

    <p>99. Если пассажир приобрел билет многоразового пользования для пригородных автомобильных перевозок в регулярном сообщении и желает иметь место для проезда, он должен заявить об этом кассиру билетной кассы и доплатить за услуги по бронированию места для проезда сидя. Кассир при наличии места обязан зарезервировать и указать пассажиру место для проезда сидя.</p>

    <p>На остановочных пунктах маршрута, не имеющих билетных касс, пассажир производит посадку в автобус при наличии свободных мест.</p>

    <p>100. Продажа билетов на пригородные автомобильные перевозки пассажиров в регулярном сообщении прекращается в кассе начального остановочного пункта маршрута за 5 минут до отправления автобуса, а в кассе промежуточного остановочного пункта маршрута или водителем в автобусе - непосредственно перед отправлением автобуса.</p>

    <p>101. Формы бланков билетов на городские и пригородные автомобильные перевозки пассажиров устанавливаются заказчиками автомобильных перевозок пассажиров в регулярном сообщении.</p>

    <p>102. Посадка и высадка пассажиров при городских автомобильных перевозках пассажиров в регулярном сообщении должны осуществляться на остановочных пунктах маршрута в порядке очереди. В автобусе с общей дверью для посадки и высадки пассажиров сначала производится высадка, затем - посадка. В автобусе с общим пассажирским салоном и двумя дверями посадка должна осуществляться через заднюю дверь, а высадка - через обе двери для пассажиров. В автобусе с тремя или четырьмя дверями посадка должна осуществляться через среднюю (средние) и заднюю двери, а высадка - через все двери для пассажиров. На конечном остановочном пункте маршрута при осуществлении контроля за наличием у пассажиров билетов и (или) документов, подтверждающих право на льготы по оплате проезда на автомобильном транспорте либо на бесплатный проезд, в автобусе, имеющем несколько пассажирских дверей, высадка пассажиров может производиться через одну дверь.</p>

    <p>103. При городских автомобильных перевозках пассажиров в регулярном сообщении через переднюю дверь автобуса, имеющего несколько пассажирских дверей, разрешается входить пассажирам с детьми дошкольного возраста, беременным женщинам, инвалидам, престарелым гражданам, а также контролирующим лицам.</p>

    <p>104. Посадка и высадка пассажиров при пригородных автомобильных перевозках пассажиров в регулярном сообщении должна осуществляться на остановочных пунктах маршрута в порядке очереди.</p>

    <p>105. Посадка пассажиров в автобус, имеющий несколько пассажирских дверей, должна осуществляться через переднюю дверь, а высадка - через все двери для пассажиров. Посадка пассажиров в автобус с одной пассажирской дверью должна производиться после окончания высадки пассажиров.</p>

