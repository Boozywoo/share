    <p>сертификаты экологической и другой безопасности транспортного средства (при выполнении перевозок в государства, в которых предъявляются соответствующие требования);</p>

    <p>схему маршрута, расписание движения, график работы водителей и билетно-учетный лист - при выполнении перевозки пассажиров в регулярном сообщении;</p>

    <p>формуляр - документ, применяемый при выполнении перевозки пассажиров в нерегулярном сообщении и содержащий список пассажиров, сведения об автомобильном перевозчике, автобусе, его экипаже и маршруте перевозки (далее - формуляр), по форме, утверждаемой Министерством транспорта и коммуникаций;</p>

    <p>разрешение на выполнение международных автомобильных перевозок пассажиров в регулярном сообщении или на проезд по территории иностранных государств (при необходимости).</p>

