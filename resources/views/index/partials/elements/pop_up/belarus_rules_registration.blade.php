    <p align="center">ПОРЯДОК ОФОРМЛЕНИЯ ПРЕТЕНЗИЙ И ИСКОВ</p>

    <p></p>

    <p>251. В случае утраты или порчи багажа, повреждения багажа и (или) ручной клади пассажира по вине автомобильного перевозчика составляется акт об утрате или порче багажа, ручной клади, который является основанием для возмещения причиненного ущерба в установленном порядке.</p>

    <p>252. Акт об утрате или порче багажа, ручной клади составляется представителем оператора пассажирского терминала, а в предусмотренных законодательством случаях - уполномоченными государственными организациями и подписывается пассажиром и представителем автомобильного перевозчика.</p>

    <p>253. Претензия может быть предъявлена автомобильному перевозчику в течение трех месяцев со дня утраты багажа, или повреждения багажа и (или) ручной клади, или причинения вреда здоровью пассажира.</p>

    <p>254. В случае болезни пассажира срок предъявления претензии продлевается до выздоровления пассажира.</p>

    <p>255. Автомобильный перевозчик обязан в течение 30 дней со дня предъявления претензии со всеми необходимыми документами рассмотреть ее и сообщить заявителю о результатах. В случае полного или частичного отклонения претензии автомобильный перевозчик обязан в своем уведомлении указать причины и возвратить документы, приложенные к претензии.</p>

    <p>256. Срок исковой давности по требованиям, вытекающим из договора об автомобильной перевозке пассажиров, составляет три года с даты, когда лицо, вред здоровью которого был причинен или нанесен ущерб имуществу, узнало или должно было узнать об этом.</p>

