    <p>вести учет фактического наличия автомобилей-такси, осуществляющих прием и передачу заказов при помощи средств электросвязи, по времени суток;</p>

    <p>осуществлять централизованный прием от пассажиров и передачу на исполнение водителям автомобилей-такси заказов на перевозки;</p>

    <p>вести учет и в течение 6 месяцев хранить информацию о полученных и выполненных заказах на перевозки автомобилями-такси (дата и время принятия заказа от пассажира, место подачи автомобиля-такси, дата и время передачи заказа водителю автомобиля-такси);</p>

    <p>заключить с местным исполнительным и распорядительным органом или уполномоченным им оператором перевозок пассажиров автомобилями-такси договор об организации автомобильных перевозок пассажиров;</p>

    <p>вести учет фактически исполненных заказов водителями автомобилей-такси;</p>

    <p>представлять по требованию местного исполнительного и распорядительного органа или уполномоченного им оператора автомобильных перевозок пассажиров автомобилями-такси информацию об организации и выполнении автомобильных перевозок пассажиров автомобилями-такси.</p>

    <p>173. Водитель автомобиля-такси обязан:</p>

    <p>знать в пределах обслуживаемой территории расположение и названия улиц и основных объектов, расположение стоянок автомобилей-такси, пунктов оказания медицинской помощи, опасные участки улично-дорожной сети, а также номера телефонов оператора автомобильных перевозок пассажиров, диспетчера такси, автомобильного перевозчика, милиции и пунктов скорой медицинской помощи;</p>

    <p>быть вежливым и доброжелательным в общении с пассажирами и контролирующими лицами;</p>

    <p>доставлять пассажиров по указанным ими адресам в пределах обслуживаемой территории;</p>

    <p>находиться в салоне или непосредственной близости от автомобиля-такси при ожидании заказа на выполнение перевозки на стоянке, специально предназначенной для автомобиля-такси;</p>

    <p>осуществлять посадку пассажиров на стоянке в порядке очереди;</p>

    <p>останавливать автомобиль-такси для выполнения перевозки по сигналу потенциального пассажира, если автомобиль-такси не задействован на обслуживании пассажиров и остановка не противоречит Правилам дорожного движения и требованиям безопасности дорожного движения;</p>

    <p>при обнаружении неисправности автомобиля-такси, таксометра принять меры по их устранению (если это не связано с нарушением их пломбировки) или доставить автомобиль к месту ремонта;</p>

    <p>при выявлении неисправностей, не позволяющих продолжить обслуживание пассажира, произвести с ним расчет по показаниям таксометра и оказать ему при необходимости содействие в продолжении поездки;</p>

    <p>после посадки пассажира для совершения поездки и уточнения у пассажира пункта назначения начинать движение с включенным таксометром;</p>

    <p>следовать по кратчайшему или иному маршруту движения, согласованному с пассажиром, с соблюдением Правил дорожного движения;</p>

    <p>вносить изменения в маршрут движения с согласия или по инициативе пассажира;</p>

    <p>по окончании перевозки, выполненной с условием предварительной оплаты стоимости перевозки, выдать пассажиру кассовый чек и произвести с ним расчет (с учетом суммы, внесенной при посадке);</p>

    <p>по окончании перевозки по безналичной оплате выдать пассажиру кассовый чек, получить от него подтверждение о выполненной перевозке в порядке, определенном автомобильным перевозчиком;</p>

    <p>после расчета за выполненную перевозку водитель должен выдать пассажиру багаж, а при обнаружении забытых пассажиром вещей принять все необходимые меры по их возвращению владельцу;</p>

    <p>выполнять полученные от диспетчера такси заказы на перевозки пассажиров;</p>

    <p>передавать диспетчеру такси информацию о месте нахождения автомобиля-такси после выполнения каждого заказа;</p>

    <p>соблюдать другие требования, установленные настоящими Правилами и иными актами законодательства.</p>

    <p>174. Водитель автомобиля-такси имеет право:</p>

    <p>требовать от пассажиров соблюдения общественного порядка;</p>

    <p>прекратить поездку при нарушении пассажиром общественного порядка, произвести с ним расчет, высадить его либо обратиться в органы внутренних дел;</p>

    <p>обращаться в органы внутренних дел за помощью для установления личности пассажира, уклоняющегося от оплаты проезда;</p>

    <p>получать от пассажира до начала обслуживания предварительную оплату в размере, не превышающем предполагаемую стоимость поездки и простоя в ожидании;</p>

    <p>считать заказ несостоявшимся, если заказчик перевозки на автомобиле-такси не явился в согласованное с ним место по истечении 10 минут после назначенного времени.</p>

    <p>175. Водителю автомобиля-такси запрещается:</p>

    <p>перевозить пассажиров с неисправным или выключенным таксометром;</p>

    <p>перевозить пассажиров и их багаж и (или) ручную кладь в количестве, превышающем нормы, установленные технической характеристикой автомобиля-такси;</p>

    <p>производить посадку пассажиров без согласия лица, заключившего первым договор об автомобильной перевозке пассажиров на автомобиле-такси;</p>

    <p>требовать от пассажира плату сверх показаний таксометра;</p>

    <p>закрывать таксометр и его составные части какими-либо предметами;</p>

    <p>выполнять автомобильные перевозки, не связанные с осуществлением лицензируемой деятельности на транспортном средстве, оборудованном и (или) оформленном как автомобиль-такси;</p>

    <p>курить во время движения автомобиля-такси.</p>

    <p>176. Диспетчер такси имеет право:</p>

    <p>получать информацию от водителей о месте нахождения автомобиля-такси после выполнения каждого заказа;</p>

    <p>осуществлять контроль за исполнением водителями переданных им заказов на перевозки автомобилями-такси;</p>

    <p>принимать участие в рассмотрении обращений пассажиров о качестве выполненных услуг водителями автомобилей-такси;</p>

    <p>вносить в местный исполнительный и распорядительный орган или уполномоченному им оператору автомобильных перевозок пассажиров автомобилями-такси предложения о совершенствовании организации перевозок пассажиров на автомобилях-такси.</p>

    <p>177. Пассажир автомобиля-такси обязан:</p>

    <p>по окончании поездки оплатить перевозку согласно показаниям таксометра;</p>

    <p>не отвлекать водителя во время движения и соблюдать общественный порядок в автомобиле-такси;</p>

    <p>согласовывать с водителем время и место ожидания;</p>

    <p>в случае несвоевременного снятия заказа на перевозку оплатить сумму в соответствии с показаниями таксометра прибывшего автомобиля-такси;</p>

    <p>возместить стоимость подачи транспортного средства в случае несвоевременного отказа от заказанной перевозки автомобилем-такси;</p>

    <p>соблюдать другие требования, установленные настоящими Правилами и иными актами законодательства.</p>

    <p>178. Пассажир автомобиля-такси имеет право:</p>

    <p>нанимать автомобиль-такси для совершения поездки в пункт назначения в пределах установленной территории обслуживания;</p>

    <p>заказывать (в том числе по телефону) автомобиль-такси с указанием места и времени его прибытия;</p>

    <p>провозить с собой бесплатно других пассажиров, их багаж и ручную кладь в количестве, не превышающем нормы, установленной технической характеристикой автомобиля-такси;</p>

    <p>требовать от водителя автомобиля-такси движения по кратчайшему или другому разрешенному для проезда маршруту к пункту назначения;</p>

    <p>при оформлении заказа на поездку в автомобиле-такси получать информацию об ориентировочной стоимости и условиях предоставляемых услуг;</p>

    <p>отменить ранее сделанный заказ на перевозку, но не позже чем за 30 минут до установленного времени прибытия автомобиля-такси, сообщив об этом в пункт приема заказов;</p>

    <p>провозить домашних животных (собак, кошек и других) и птиц в клетках, ящиках, мешках или сумках с водонепроницаемым адсорбирующим дном. Собака в наморднике и на коротком поводке может перевозиться пассажиром не моложе 16 лет (собака потенциально опасной породы перевозится только совершеннолетним пассажиром). При этом пассажир берет на себя ответственность в случае порчи имущества автомобильного перевозчика.</p>

    <p>179. Право пользования автомобилями-такси вне очереди предоставляется беременным женщинам, пассажирам с детьми дошкольного возраста и инвалидам с явными признаками инвалидности.</p>

