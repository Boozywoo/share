    <p align="center">Глава 14</p>

    <p align="center">ПРАВА И ОБЯЗАННОСТИ АВТОМОБИЛЬНЫХ ПЕРЕВОЗЧИКОВ</p>

    <p></p>

    <p>218. Автомобильный перевозчик или его представитель при выполнении автомобильных перевозок пассажиров имеет право:</p>

    <p>требовать от заказчика городских, пригородных и междугородных внутриобластных автомобильных перевозок пассажиров в регулярном сообщении содержания автомобильных дорог и улиц населенных пунктов в надлежащем состоянии;</p>

    <p>обращаться в местные исполнительные и распорядительные органы по вопросам организации ремонта автомобильных дорог и улиц населенных пунктов, по которым проходит установленный маршрут движения автобусов;</p>

    <p>прекращать движение на маршруте при неисполнении заказчиком или оператором автомобильных перевозок пассажиров договорных обязательств, предупредив об этом заказчика или оператора автомобильных перевозок пассажиров не позднее чем за 7 рабочих дней;</p>

    <p>сокращать объем автомобильных перевозок пассажиров транспортом общего пользования в соответствии с фактическими объемами финансирования, если иное не установлено в договоре об организации автомобильных перевозок пассажиров;</p>

    <p>требовать от пассажира соблюдения настоящих Правил, условий договора об автомобильных перевозках пассажиров и иных актов законодательства;</p>

    <p>получать плату за автомобильную перевозку пассажира, провоз багажа и ручной клади по установленным тарифам, если иное не предусмотрено законами и актами Президента Республики Беларусь;</p>

    <p>проверять у пассажиров билеты и документы, подтверждающие право на льготы по оплате проезда на автомобильном транспорте;</p>

    <p>проверять при международных автомобильных перевозках пассажиров соответствие данных, указанных в билете или формуляре, данным документа, удостоверяющего личность, или свидетельства о регистрации ходатайства о предоставлении статуса беженца или дополнительной защиты в Республике Беларусь либо свидетельства о предоставлении дополнительной защиты в Республике Беларусь (в случае отсутствия документа, удостоверяющего личность) пассажира;</p>

    <p>(в ред. постановлений Совмина от 23.12.2008 N 2010, от 19.05.2009 N 646)</p>

    <p>(см. текст в предыдущей редакции)</p>

    <p>отменять рейс и приостанавливать движение транспортного средства в случае возникновения угрозы жизни или здоровью пассажиров и членов экипажа;</p>

    <p>ограничить или приостановить автомобильные перевозки пассажиров в регулярном сообщении при возникновении стихийного бедствия, эпидемии или другой чрезвычайной ситуации, оповестив об этом население через средства массовой информации, а в экстренных случаях при прямой угрозе безопасности перевозок - немедленно;</p>

    <p>сделать в багажной квитанции отметку о состоянии багажа или его упаковки с согласия пассажира. Если пассажир возражает, то автомобильный перевозчик может отказать в приеме багажа к автомобильной перевозке;</p>

    <p>отказать пассажиру в перевозке багажа и (или) ручной клади, если есть основания сомневаться в их безопасной перевозке или сумме объявленной ценности багажа, когда пассажир отказывается предъявить багаж и (или) ручную кладь для осмотра или доказать документально объявленную ценность багажа;</p>

    <p>отказать пассажиру в перевозке сверхнормативного числа мест маломерного и среднемерного багажа и (или) ручной клади, а также в перевозке крупномерных мест багажа и (или) ручной клади;</p>

    <p>сертифицировать свою транспортную деятельность по перевозке пассажиров;</p>

    <p>требовать возмещения ущерба, причиненного ему по вине пассажира, в соответствии с актами законодательства и заключенным договором об автомобильной перевозке пассажиров;</p>

    <p>отказать пассажиру в автомобильной перевозке в случаях, предусмотренных статьей 31 Закона Республики Беларусь "Об автомобильном транспорте и автомобильных перевозках".</p>

    <p>219. Автомобильный перевозчик при выполнении автомобильных перевозок пассажиров обязан:</p>

    <p>выполнять автомобильные перевозки пассажиров в соответствии с настоящими Правилами, Правилами дорожного движения и иными актами законодательства;</p>

    <p>ознакомить водителя со схемой маршрута и расписанием движения;</p>

    <p>обеспечивать наличие на пассажирских терминалах и у водителей необходимых технологических документов (схем маршрутов, расписаний движения и других);</p>

    <p>иметь карточку маршрута или разрешение на выполнение автомобильных перевозок пассажиров в регулярном сообщении;</p>

    <p>своевременно подавать транспортное средство и обеспечивать перевозку пассажиров в нерегулярном сообщении в сроки в соответствии с заключенным договором о фрахтовании для автомобильных перевозок пассажиров, а в регулярном сообщении при городских и пригородных автомобильных перевозках пассажиров - не ранее чем за 3 минуты и не позже чем на 5 минут относительно времени по расписанию; при междугородных автомобильных перевозках пассажиров - не ранее чем за 5 минут и не позже чем на 10 минут относительно времени по расписанию. В начальных пунктах пригородных, междугородных и международных перевозок пассажиров в регулярном сообщении подавать автобус для посадки пассажиров за 10 минут до времени отправления по расписанию;</p>

    <p>обеспечивать техническое и санитарное состояние, оснащение и оформление транспортного средства в соответствии с установленными требованиями;</p>

    <p>обеспечивать продажу билетов через билетные кассы и в автобусах;</p>

    <p>обеспечивать безопасность выполнения автомобильных перевозок пассажиров и дорожного движения, соблюдать экологические и санитарно-гигиенические нормы;</p>

    <p>обеспечивать проверку у пассажиров наличия и соответствия билетов и багажных квитанций (кассовых чеков) на перевозку багажа и провоз ручной клади;</p>

    <p>обеспечивать соблюдение законодательных актов о предоставлении пассажирам льгот по оплате проезда на автомобильном транспорте;</p>

    <p>осуществлять обязательное страхование в порядке, установленном законодательством;</p>

    <p>выполнять требования контролирующих лиц;</p>

    <p>допускать при выполнении автомобильной перевозки пассажиров нахождение в транспортном средстве только водителей этого транспортного средства, пассажиров с билетами, кондукторов и контролеров;</p>

    <p>не допускать нахождения в транспортном средстве вещей, не принадлежащих членам экипажа или пассажирам;</p>

    <p>не допускать при автомобильной перевозке пассажиров, их багажа и ручной клади переполнения салона и превышения технически допустимой общей массы транспортного средства, установленной заводом-изготовителем, а также разрешенных максимальной массы транспортного средства, нагрузок на оси и установленного числа перевозимых пассажиров;</p>

    <p>перевозить в автобусах с багажными отсеками среднемерный и крупномерный багаж только в багажных отсеках;</p>

    <p>обеспечивать общедоступность информации об автомобильных перевозках пассажиров и условиях их выполнения, о тарифах на перевозки, а при международных перевозках - о необходимых документах и других требованиях;</p>

    <p>информировать пассажиров о том, что за все скрытые и запрещенные к автомобильной перевозке предметы, а также за неправильно оформленные документы они несут личную ответственность;</p>

    <p>оказывать пассажирам на бесплатной и платной основе объявленные дополнительные услуги;</p>

    <p>использовать в случаях, предусмотренных актами законодательства, кассовый суммирующий аппарат транспорта, таксометр, контрольное устройство (тахограф);</p>

    <p>обеспечивать доставку пассажиров в пункты назначения в случае прерывания автомобильной перевозки пассажиров по обстоятельствам, зависящим от автомобильного перевозчика;</p>

    <p>оказывать содействие на платной основе другим автомобильным перевозчикам по их просьбе в перевозке пассажиров, обслуживание которых прервано и не может быть выполнено собственными силами этих автомобильных перевозчиков;</p>

    <p>возмещать пассажирам причиненные жизни, здоровью вред или ущерб имуществу вследствие неисполнения или ненадлежащего исполнения автомобильным перевозчиком обязательств и обязанностей, вытекающих из договоров и законодательства;</p>

    <p>рассматривать обращения пассажиров по вопросам качества транспортного обслуживания и принимать соответствующие меры по совершенствованию обслуживания пассажиров;</p>

    <p>принимать в соответствии с законодательством необходимые меры по пресечению нарушений пассажирами общественного порядка и требований настоящих Правил;</p>

    <p>сообщать в правоохранительные органы об обнаружении в салоне транспортного средства предметов, оставленных без присмотра, а также о противоправных действиях пассажиров и других лиц в отношении пассажиров.</p>

    <p>220. Автомобильному перевозчику запрещается без наличия карточки маршрута или соответствующего разрешения выполнять автомобильные перевозки пассажиров в регулярном сообщении (производить посадку-высадку пассажиров на остановочных пунктах маршрута), а также заезжать на пассажирский терминал без наличия договора на пользование этим терминалом.</p>

