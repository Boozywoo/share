    <p align="center">Глава 1</p>

    <p align="center">ОБЩИЕ ПОЛОЖЕНИЯ</p>

    <p></p>

    <p>1. Настоящие Правила разработаны в соответствии с Гражданским кодексом Республики Беларусь, законами Республики Беларусь от 9 января 2002 года "О защите прав потребителей" (Национальный реестр правовых актов Республики Беларусь, 2002 г., N 10, 2/839), от 14 августа 2007 года "Об автомобильном транспорте и автомобильных перевозках" (Национальный реестр правовых актов Республики Беларусь, 2007 г., N 199, 2/1375) и иными нормативными правовыми актами Республики Беларусь. Настоящими Правилами регулируются отношения, возникающие при организации и выполнении автомобильных перевозок пассажиров, устанавливаются порядок и условия выполнения автомобильных перевозок пассажиров, их багажа и (или) ручной клади, определяются права, обязанности и ответственность пассажиров, автомобильных перевозчиков и их представителей, заказчиков и операторов автомобильных перевозок пассажиров.</p>

    <p>2. В настоящих Правилах применяются термины в значениях, определенных Законом Республики Беларусь "Об автомобильном транспорте и автомобильных перевозках", а также следующие термины и их определения:</p>

    <p>автобус категории М2 - автобус, максимальная масса которого не превышает 5000 кг;</p>

    <p>автобус категории М3 - автобус, максимальная масса которого превышает 5000 кг;</p>

    <p>автомобильные перевозки пассажиров в обычном регулярном сообщении - автомобильные перевозки, предусматривающие посадку-высадку пассажиров на всех остановочных пунктах маршрута;</p>

    <p>автомобильные перевозки пассажиров в скоростном регулярном сообщении - автомобильные перевозки, предусматривающие посадку-высадку пассажиров на меньшем числе остановочных пунктов маршрута по сравнению с перевозкой по этому же маршруту в обычном регулярном сообщении;</p>

    <p>автомобильные перевозки пассажиров в экспрессном регулярном сообщении - перевозки, предусматривающие посадку-высадку пассажиров на значительно меньшем числе остановочных пунктов маршрута по сравнению с перевозкой по этому же маршруту в обычном регулярном сообщении, в том числе перевозки без промежуточных остановок;</p>

    <p>бронирование места для проезда - предварительное резервирование места в транспортном средстве для автомобильной перевозки пассажиров в регулярном сообщении;</p>

    <p>водитель - физическое лицо, управляющее транспортным средством и являющееся при выполнении автомобильной перевозки пассажиров представителем автомобильного перевозчика;</p>

    <p>диспетчер автомобильных перевозок пассажиров автомобилями-такси (далее - диспетчер такси) - юридическое лицо, оказывающее услуги по приему и передаче заказов, в том числе с использованием средств электросвязи, на перевозки пассажиров автомобилями-такси;</p>

    <p>дополнительный рейс - рейс автобуса, введенный в дополнение к установленным рейсам на маршруте для обеспечения возрастающей потребности в перевозках пассажиров;</p>

    <p>кондуктор - уполномоченное лицо, обеспечивающее взимание платы за проезд пассажиров в регулярном сообщении и провоз ими ручной клади, а также проверку наличия у пассажиров билетов и (или) иных документов, подтверждающих право на льготы по оплате проезда на автомобильном транспорте;</p>

    <p>контролирующее лицо - лицо, уполномоченное в соответствии с законодательством на осуществление контроля за соблюдением требований законодательства об автомобильном транспорте и автомобильных перевозках пассажиров;</p>

    <p>маршрут - установленный путь следования транспортного средства между определенными пунктами;</p>

    <p>места багажа или ручной клади крупномерные - предметы, имеющие размеры более 100 x 50 x 30 см или сумму трех измерений более 180 см или массу более 40 кг;</p>

    <p>места багажа или ручной клади маломерные - предметы, имеющие размеры не более 60 x 40 x 20 см или сумму трех измерений не более 120 см и массу не более 20 кг;</p>

    <p>места багажа или ручной клади среднемерные - предметы, имеющие размеры не более 100 x 50 x 30 см или сумму трех измерений не более 180 см и массу не более 40 кг;</p>

    <p>остановочный пункт маршрута - установленное место остановки автобуса для посадки-высадки пассажиров, обозначенное и оборудованное в соответствии с действующими требованиями;</p>

    <p>оператор пассажирского терминала - юридическое лицо, осуществляющее деятельность по организации автомобильных перевозок пассажиров и оказанию услуг пассажирам и автомобильным перевозчикам на пассажирских терминалах;</p>

    <p>расписание движения - установленный временной график выполнения рейсов, включающий время прибытия и (или) отправления автобуса для каждого остановочного пункта маршрута (даты, дни недели, часы и минуты суток);</p>

    <p>рейс - одна ездка транспортного средства от начального до конечного пункта маршрута в прямом или обратном направлении;</p>

    <p>схема маршрута - графическое изображение маршрута с указанием остановочных пунктов;</p>

    <p>таксометр - зарегистрированное в налоговых органах специальное оборудование автомобиля-такси, совмещенное с кассовым суммирующим аппаратом и предназначенное для автоматического определения платы за проезд, информирования пассажира о размере платы, а также учета показателей работы и режимов использования автомобиля-такси;</p>

    <p>тариф - размер стоимости единицы услуги по перевозке пассажиров, багажа и (или) ручной клади за одну поездку, посадку, один километр проезда, один час использования, провоз одного места багажа и (или) ручной клади, объявление стоимости багажа и за другие услуги;</p>

    <p>транспортное средство - механическое транспортное средство, предназначенное по своей конструкции и оборудованию для перевозки пассажиров и багажа и (или) ручной клади (легковой автомобиль, автобус).</p>

    <p>3. Действие настоящих Правил не распространяется на организацию и выполнение автомобильных перевозок пассажиров, исключенных из сферы действия Закона Республики Беларусь "Об автомобильном транспорте и автомобильных перевозках".</p>

    <p></p>

    <p align="center">Глава 2</p>

    <p align="center">ТРЕБОВАНИЯ К ТРАНСПОРТНЫМ СРЕДСТВАМ</p>

    <p></p>

    <p>4. Транспортное средство при выполнении автомобильных перевозок пассажиров должно быть в технически исправном состоянии, что подтверждается сертификатом о прохождении государственного технического осмотра, международным сертификатом технического осмотра (при международных перевозках), разрешением на допуск транспортного средства к участию в дорожном движении, а также отметкой в путевом листе.</p>

    <p>5. Соответствие транспортных средств дополнительным экологическим и техническим требованиям должно подтверждаться сертификатами. Изменение конструкции транспортного средства должно быть сертифицировано на соответствие обязательным требованиям технических нормативных правовых актов.</p>

    <p>6. Транспортное средство должно быть оснащено (экипировано) аптечкой, огнетушителем, противооткатным упором, знаком аварийной остановки, устройством для аварийного удаления стекол, визитной карточкой, содержащей сведения о водителе и его фотографию, наименовании, адресе и телефоне автомобильного перевозчика (в салоне на видном месте при перевозках в регулярном сообщении и автомобилями-такси). В автобусе один огнетушитель должен находиться в кабине водителя, второй - в салоне автобуса.</p>

    <p>Автобусы категории М3 должны иметь не менее двух противооткатных упоров.</p>

    <p>7. Количество перевозимых пассажиров, масса их багажа и (или) ручной клади, масса транспортного средства и нагрузки на его оси не должны превышать норм, установленных заводом-изготовителем.</p>

    <p>8. Транспортные средства, зарегистрированные в Республике Беларусь, при выполнении автомобильных перевозок пассажиров должны быть оформлены и иметь регистрационные знаки в соответствии с требованиями технических нормативных правовых актов. Транспортные средства, оформленные для выполнения автомобильных перевозок пассажиров в соответствии с требованиями технических нормативных правовых актов, не могут использоваться физическими лицами для личных (бытовых, семейных и иных нужд), не связанных с осуществлением лицензируемой деятельности по выполнению автомобильных перевозок пассажиров.</p>

    <p>9. Автомобильные перевозки пассажиров в регулярном сообщении могут выполняться только автобусами. При этом в автобусах должны иметься:</p>

    <p>указатели маршрута;</p>

    <p>информационные надписи, содержащие указание числа мест для сидения, обозначение "Запасный выход", обозначение приборов экстренного управления дверями, запасными выходами, а также инструкции о пользовании ими;</p>

    <p>нумерация мест для сидения пассажиров (в автобусах, применяемых для выполнения пригородных, междугородных и международных перевозок пассажиров);</p>

    <p>устройства для гашения билетов (при применении соответствующей системы оплаты проезда);</p>

    <p>аудиосистема для оповещения пассажиров в автобусах категории М3 с числом мест для сидения пассажиров свыше 22.</p>

    <p>10. Указатели маршрута на автобусе (в том числе в виде электронных табло), используемом для выполнения перевозок в регулярном сообщении, должны содержать номер маршрута, названия конечных и узловых промежуточных пунктов маршрута, размер платы за проезд (при выполнении городских перевозок пассажиров в экспрессном сообщении).</p>

    <p>Передний указатель маршрута должен содержать номер маршрута и названия конечных остановочных пунктов (допускается указание только номера маршрута, если конструкция автобуса не позволяет разместить всю информацию). Не допускается применение переднего указателя маршрута красного цвета и (или) излучающего красный свет. Боковой указатель маршрута должен содержать номер маршрута, названия его конечных и нескольких узловых промежуточных пунктов, через которые проходит маршрут, размер платы за проезд (при выполнении городских перевозок пассажиров в экспрессном сообщении). Указатели маршрутов, имеющих одни и те же конечные пункты, но различные пути следования между ними, должны отличаться указанными на них узловыми промежуточными пунктами. На заднем указателе маршрута приводится только номер маршрута. Установка заднего указателя обязательна только при выполнении городских автомобильных перевозок пассажиров автобусами категории М3.</p>

    <p>11. В автобусах категории М3, используемых для выполнения городских перевозок пассажиров в регулярном сообщении, в передней части салона должны быть специально обозначены места (не менее четырех) для пассажиров с детьми дошкольного возраста, беременных женщин, инвалидов и лиц престарелого возраста.</p>

    <p>12. Для выполнения междугородных внутриобластных и межобластных автомобильных перевозок (далее - междугородные автомобильные перевозки) пассажиров должны использоваться автобусы с сиденьями, удобными для поездок на дальние расстояния.</p>

    <p>13. Для выполнения международных автомобильных перевозок пассажиров должны использоваться автобусы, имеющие:</p>

    <p>сиденья, удобные для поездок на дальние расстояния;</p>

    <p>багажный отсек объемом не менее 0,1 куб.м в расчете на каждое пассажирское место для сидения (при числе мест для сидения свыше 22);</p>

    <p>контрольное устройство (тахограф) для регистрации данных о движении автобуса и работе его водителей;</p>

    <p>не менее 23 пассажирских мест для сидения (при перевозках в регулярном сообщении);</p>

    <p>иное оборудование, обеспечивающее комфортабельность поездки пассажиров.</p>

    <p>14. Информация на указателях маршрута автобусов, используемых для выполнения международных автомобильных перевозок пассажиров в регулярном сообщении, должна быть на двух языках - русском и языке государства конечного пункта маршрута.</p>

    <p>15. В качестве автомобилей-такси для выполнения перевозок пассажиров должны применяться легковые автомобили, имеющие:</p>

    <p>число пассажирских мест для сидения не менее трех, но не более шести;</p>

    <p>таксометр;</p>

    <p>соответствующие отличительные знаки согласно требованиям технических нормативных правовых актов;</p>

    <p>конструкцию и расположение дверей и сидений, обеспечивающие удобную посадку-высадку пассажиров (не менее двух пассажирских дверей с правой стороны автомобиля).</p>

    <p>16. В автомобиле-такси должна быть размещена информация о тарифах на проезд.</p>

    <p>17. При выполнении городских автомобильных перевозок пассажиров в регулярном сообщении в салоне автобуса должна быть размещена информация о стоимости поездки и размере штрафа за безбилетный проезд.</p>

    <p>18. Транспортные средства, в которых производится оплата пассажирами проезда наличными денежными средствами (кроме случаев разрешенной продажи бланочных билетов), должны быть оснащены кассовыми суммирующими аппаратами транспорта или таксометрами, включенными в Государственный реестр моделей (модификаций) кассовых суммирующих аппаратов и специальных компьютерных систем и зарегистрированными в установленном порядке в соответствующем налоговом органе.</p>

