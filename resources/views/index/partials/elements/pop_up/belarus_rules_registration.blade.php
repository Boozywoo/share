    <p>маршрут движения автобуса (номер маршрута);</p>

    <p>дата и время начала поездки (при продаже билета в автобусе - необязательно);</p>

    <p>номер места для сидения;</p>

    <p>стоимость проезда;</p>

    <p>дата и время выдачи билета.</p>

