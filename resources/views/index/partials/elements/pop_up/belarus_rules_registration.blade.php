    <p>106. Междугородные автомобильные перевозки пассажиров могут быть организованы в обычном регулярном, скоростном регулярном и экспрессном регулярном сообщениях.</p>

    <p>107. При междугородных автомобильных перевозках пассажиров в регулярном скоростном сообщении на маршруте протяженностью до 120 км должно быть не более двух промежуточных остановочных пунктов, от 120 до 240 км - не более трех и при свыше 240 км - не более четырех промежуточных остановочных пунктов, расположенных на пассажирских терминалах.</p>

    <p>108. При междугородных автомобильных перевозках пассажиров в регулярном экспрессном сообщении автобусами М3 на маршруте протяженностью до 120 км промежуточных остановочных пунктов не должно быть, при протяженности от 120 до 240 км - не более двух и свыше 240 км - не более трех промежуточных остановочных пунктов, расположенных на пассажирских терминалах.</p>

    <p>109. При междугородных автомобильных перевозках пассажиров в регулярном экспрессном сообщении автобусами категории М2 автобус останавливается на промежуточных остановочных пунктах маршрута только при необходимости высадки и посадки пассажиров либо перерыва в работе водителя.</p>

    <p>110. Заказчик либо оператор автомобильных перевозок пассажиров в регулярном сообщении или Министерство транспорта и коммуникаций имеют право:</p>

    <p>вносить в расписание движения автобусов изменения и дополнения;</p>

    <p>расторгнуть в одностороннем порядке с автомобильным перевозчиком договор об организации перевозок пассажиров в регулярном сообщении в соответствии с условиями этого договора, а также при грубых нарушениях автомобильным перевозчиком законодательства о лицензировании или установленных лицензионных требований и условий осуществления лицензируемой деятельности.</p>

    <p>111. Начальный и конечный остановочные пункты маршрута междугородной автомобильной перевозки пассажиров в регулярном сообщении, расположенные на территории города или поселка городского типа, должны находиться на пассажирском терминале.</p>

    <p>112. Промежуточный остановочный пункт маршрута междугородной автомобильной перевозки в регулярном сообщении, расположенный вне территории населенного пункта городского типа (на автомобильной дороге), должен иметь переходно-скоростные полосы, посадочную площадку с усовершенствованным покрытием, ограниченную бордюрным камнем со стороны проезжей части, навес или павильон и должен быть обозначен соответствующим знаком.</p>

    <p>113. Промежуточный остановочный пункт маршрута междугородной автомобильной перевозки пассажиров в регулярном сообщении может быть совмещен с остановочным пунктом маршрута городских или пригородных перевозок пассажиров в регулярном сообщении.</p>

    <p>114. При междугородных автомобильных перевозках пассажиров в регулярном сообщении должны применяться билеты, оформленные с применением кассовых суммирующих аппаратов транспорта или специальных компьютерных систем либо на бланках установленной формы.</p>

    <p>115. В билете на междугородную автомобильную перевозку в регулярном сообщении должны указываться:</p>

    <p>наименование автомобильного перевозчика;</p>

    <p>назначение билета;</p>

    <p>серия (на бланочном билете) и номер билета;</p>

