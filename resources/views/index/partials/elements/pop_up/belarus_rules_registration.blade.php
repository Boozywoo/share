    <p align="center">ПРАВА И ОБЯЗАННОСТИ ЧЛЕНОВ ЭКИПАЖА ТРАНСПОРТНОГО СРЕДСТВА</p>

    <p></p>

    <p>221. Член экипажа транспортного средства (водитель, кондуктор) в соответствии со своими полномочиями имеет право:</p>

    <p>продавать билеты и багажные квитанции (если их продажа не обеспечена билетными кассами);</p>

    <p>контролировать оплату проезда;</p>

    <p>не допустить к поездке или высадить на ближайшей остановке пассажира в случаях, когда автомобильный перевозчик имеет право отказать пассажиру в перевозке;</p>

    <p>выдавать багаж на особых условиях, если не предъявлена багажная квитанция или отрывная часть багажной бирки;</p>

    <p>присутствовать во время осмотра транспортного средства и багажа уполномоченными лицами компетентных органов;</p>

    <p>контролировать соблюдение и требовать от пассажиров выполнения настоящих Правил;</p>

    <p>обжаловать в установленном законодательством порядке неправомерные действия контролирующих лиц;</p>

    <p>вносить предложения о совершенствовании перевозок, работы пассажирских терминалов, повышении качества обслуживания пассажиров, безопасности автомобильных перевозок пассажиров, об улучшении условий труда и отдыха, эффективном использовании транспортных средств.</p>

    <p>222. Член экипажа транспортного средства (водитель, кондуктор) в соответствии со своими полномочиями обязан:</p>

    <p>знать и выполнять настоящие Правила, правила техники безопасности и пожарной безопасности;</p>

    <p>соблюдать правила дорожного движения государства проезда, типовые инструкции водителей транспортных средств, правила технической эксплуатации транспортного средства и безопасности движения (водитель);</p>

    <p>проверять перед началом работы и систематически во время работы техническое состояние транспортного средства, контролировать перед началом движения закрытие дверей и багажных отсеков (водитель);</p>

    <p>выполнять правила пограничного, таможенного, санитарного и других видов контроля при выполнении международной перевозки пассажиров;</p>

    <p>выполнять оперативные указания компетентных органов и уполномоченных ими организаций;</p>

    <p>обеспечивать соблюдение пассажирами настоящих Правил в части, их касающейся;</p>

    <p>выполнять установленный режим труда и отдыха;</p>

    <p>проверять наличие и соответствие билетов и (или) документов, подтверждающих право на льготы по оплате проезда на автомобильном транспорте, багажных квитанций на перевозку багажа и оплату провоза ручной клади сверх нормы бесплатного провоза;</p>

    <p>обеспечивать безопасную автомобильную перевозку пассажиров;</p>

    <p>оказывать помощь пассажирам по размещению ручной клади (кроме городских автомобильных перевозок в регулярном сообщении);</p>

    <p>контролировать размещение ручной клади в салоне транспортного средства;</p>

    <p>принимать от пассажиров, размещать (кроме крупномерных мест) и выдавать им багаж;</p>

    <p>останавливать транспортное средство по требованию лиц, имеющих на то право, и предъявлять им для контроля требуемые документы, дополнительное и специальное оборудование;</p>

    <p>оказать при необходимости пассажиру доврачебную помощь или вызвать скорую медицинскую помощь либо принять меры по доставке этого пассажира в ближайшее лечебное учреждение;</p>

    <p>в случае дорожно-транспортного происшествия действовать в соответствии с Правилами дорожного движения;</p>

    <p>при автомобильных перевозках пассажиров в регулярном сообщении обеспечивать движение строго по маршруту с посадкой-высадкой пассажиров в установленных для этого местах после полной остановки у передней границы остановочного пункта маршрута (или как можно ближе к ней) в пределах остановочного пункта вдоль продольного края посадочной площадки или тротуара (не далее 0,3 м по горизонтали от продольного края посадочной площадки или тротуара улицы населенного пункта или дороги до подножки автобуса). При вынужденных посадке-высадке в других местах не создавать препятствий для движения других транспортных средств и пешеходов, выполнить посадку с тротуара и высадку на тротуар или обочину, исключив при возможности необходимость пересечения пассажирами проезжей части дороги;</p>

    <p>осуществлять продажу билетов только на остановочном пункте маршрута (в случае продажи билетов водителем);</p>

    <p>начинать движение транспортного средства только с закрытыми дверями и открывать их для посадки-высадки на остановках;</p>

    <p>проверять перед началом работы техническую исправность кассового суммирующего аппарата транспорта или таксометра;</p>

    <p>содержать в надлежащем санитарном состоянии транспортное средство, его салон и багажный отсек;</p>

    <p>включать отопление салона при температуре воздуха ниже +5 °С или согласно указаниям автомобильного перевозчика;</p>

    <p>знать расположение остановочных пунктов маршрута и опасных участков на маршруте, а также пунктов оказания технической и медицинской помощи;</p>

    <p>не допускать переполнения салона и багажного отсека сверх установленной нормы для конкретного транспортного средства;</p>

    <p>объявлять на маршруте названия текущей и следующей остановок по прибытии на остановочный пункт (при междугородных и международных перевозках - время стоянки на нем) и следующей остановки до закрытия дверей и отправления автобуса, а об изменении маршрута объявлять на каждом остановочном пункте;</p>

    <p>предупреждать пассажиров о закрытии дверей транспортного средства;</p>

    <p>выполнять автомобильную перевозку пассажиров в регулярном сообщении в соответствии с утвержденным расписанием движения, а в нерегулярном сообщении - в соответствии со сроками, установленными договором о фрахтовании для автомобильной перевозки пассажиров;</p>

    <p>периодически напоминать пассажирам о необходимости оплаты проезда (при городских перевозках);</p>

    <p>предупреждать пассажиров о предстоящей проверке билетов и оказывать содействие контролирующим лицам в осуществлении контроля;</p>

    <p>информировать пассажиров об условиях автомобильной перевозки и требованиях к их поведению;</p>

    <p>быть вежливым, доброжелательным и тактичным при обслуживании пассажиров;</p>

    <p>иметь надлежащим образом оформленные документы на право пересечения государственных границ при международных автомобильных перевозках, своевременно информировать пассажиров о предстоящем прохождении контроля при пересечении государственных границ;</p>

    <p>сдавать оставленные в транспортном средстве вещи, документы или деньги на ответственное хранение по акту о сдаче вещей;</p>

    <p>оказывать содействие пассажиру в его дальнейшей поездке при вынужденном прекращении перевозки.</p>

    <p></p>

    <p align="center">Глава 16</p>

    <p align="center">ОБЕСПЕЧЕНИЕ БЕЗОПАСНОГО ВЫПОЛНЕНИЯ АВТОМОБИЛЬНЫХ</p>

    <p align="center">ПЕРЕВОЗОК ПАССАЖИРОВ</p>

    <p></p>

    <p>223. Безопасное выполнение перевозок пассажиров должно обеспечиваться автомобильными перевозчиками, заказчиками и операторами автомобильных перевозок пассажиров, дорожными организациями и другими юридическими лицами, осуществляющими транспортную деятельность по перевозке пассажиров.</p>

    <p>224. Ответственность за организацию работы по обеспечению автомобильных перевозок пассажиров возлагается на руководителей организаций и индивидуальных предпринимателей, являющихся автомобильными перевозчиками.</p>

    <p>225. При непригодности дорожных условий для осуществления автомобильных перевозок пассажиров или несоответствия их установленным требованиям автомобильный перевозчик должен принимать меры по приостановлению и (или) изменению маршрута перевозок.</p>

    <p>226. К водителям, выполняющим автомобильные перевозки пассажиров, предъявляются требования, установленные настоящими Правилами и другими актами законодательства. Автомобильный перевозчик может предъявлять к водителям дополнительные требования по обеспечению безопасности дорожного движения и перевозок пассажиров, не противоречащие действующему законодательству и настоящим Правилам.</p>

    <p>227. Автомобильному перевозчику запрещается понуждать или поощрять водителей транспортных средств к нарушению требований безопасности дорожного движения.</p>

    <p>228. Графики работы водителей должны обеспечивать соблюдение норм законодательства Республики Беларусь и международных договоров в части режима труда и отдыха водителей.</p>

    <p>Работа водителей в командировке должна быть организована в соответствии с действующим законодательством.</p>

    <p>229. Автомобильный перевозчик должен обеспечить нахождение принадлежащих ему транспортных средств при межсменном хранении на специально предназначенных для этого охраняемых стоянках.</p>

    <p>230. Автомобильный перевозчик должен соблюдать правила технической эксплуатации транспортных средств в соответствии с инструкциями их организаций-изготовителей. Транспортное средство должно проходить плановое техническое обслуживание с установленной периодичностью, о чем у автомобильного перевозчика должны быть подтверждающие документы.</p>

    <p>231. Автомобильный перевозчик должен обеспечить контроль технического состояния транспортного средства с отметкой в путевом листе перед началом работы. Допуск к автомобильным перевозкам пассажиров транспортных средств, находящихся в технически неисправном состоянии, запрещается.</p>

    <p>232. Автомобильный перевозчик должен предъявлять эксплуатируемые им транспортные средства для их государственного технического осмотра в порядке и с периодичностью, установленными законодательством.</p>

    <p>233. Автомобильный перевозчик имеет право при выполнении автомобильной перевозки пассажиров осуществлять контроль за соблюдением положений нормативных правовых актов, регламентирующих безопасность перевозки.</p>

    <p>234. Автомобильный перевозчик для обеспечения безопасного выполнения автомобильных перевозок пассажиров обязан:</p>

    <p>организовать работу по выполнению требований нормативных правовых актов в области обеспечения безопасности дорожного движения и автомобильных перевозок пассажиров;</p>

    <p>иметь службу безопасности движения или назначить на должность, связанную с обеспечением безопасных автомобильных перевозок пассажиров, ответственное лицо со специальной подготовкой;</p>

    <p>организовывать повышение квалификации своих работников по вопросам обеспечения безопасности дорожного движения и автомобильных перевозок пассажиров, а также соблюдения правил охраны труда при этих перевозках;</p>

    <p>организовывать и контролировать работу водителей, проводить их стажировку;</p>

    <p>организовать и оснастить учебно-наглядными материалами, плакатами, тренажерами помещение или место для проведения учебной и профилактической работы по предупреждению дорожно-транспортных происшествий;</p>

    <p>иметь нормативные правовые акты, методические и информационные материалы для проведения профилактической работы по организации безопасных автомобильных перевозок пассажиров;</p>

    <p>обеспечить постоянный контроль за соблюдением действующих нормативов по срокам управления, работы, перерывов в работе и отдыха водителей, анализировать работу водителей по карточкам маршрута и (или) регистрационным листкам (тахограммам) в отношении режима их работы и соблюдения скоростных режимов движения;</p>

    <p>обеспечить соответствие технического состояния транспортных средств требованиям безопасности дорожного движения, не допуская к эксплуатации транспортные средства с неисправностями;</p>

    <p>организовать проведение служебного расследования, учета и анализа дорожно-транспортных происшествий с участием транспортных средств автомобильного перевозчика, а также обеспечить выявление причин, способствующих их возникновению;</p>

    <p>проинструктировать работников о порядке действий при получении информации о совершении или угрозе совершения акта терроризма, при управлении в сложных дорожных и метеорологических условиях, при дорожно-транспортном происшествии, пожаре и в других чрезвычайных ситуациях;</p>

    <p>обеспечить учет данных о квалификации водителей, их стаже работы на определенных типах транспортных средств, сроках прохождения медицинского переосвидетельствования, участии в дорожно-транспортных происшествиях, допущенных нарушениях Правил дорожного движения и требований безопасности автомобильных перевозок пассажиров;</p>

    <p>проверять знание водителями Правил дорожного движения, настоящих Правил и других актов законодательства по вопросам безопасности дорожного движения и автомобильных перевозок;</p>

    <p>пропагандировать необходимость соблюдения Правил дорожного движения и требований безопасности автомобильных перевозок пассажиров, поощрять работников, обеспечивающих безаварийную работу;</p>

    <p>планировать и проводить мероприятия, направленные на соблюдение требований Правил дорожного движения и безопасной перевозки пассажиров, доводить до водителей информацию об изменении Правил дорожного движения;</p>

    <p>оперативно доводить до водителей сведения о причинах и обстоятельствах известных ему дорожно-транспортных происшествий, а также информацию об изменениях условий движения и работы на маршрутах автомобильных перевозок пассажиров;</p>

    <p>проводить мероприятия, направленные на повышение профессионального мастерства водителей, в том числе по безопасной автомобильной перевозке пассажиров;</p>

    <p>осуществлять контроль за обеспечением безопасности дорожного движения и автомобильных перевозок пассажиров в процессе выполнения этих перевозок, в частности, соблюдения скоростных режимов, норм вместимости транспортных средств и маршрутов движения;</p>

    <p>осуществлять контроль за автомобильной перевозкой групп детей автобусами;</p>

    <p>организовать в установленном порядке проведение медицинского обследования водителей с отметкой в путевом листе перед началом работы, а при необходимости - во время работы и (или) после ее окончания;</p>

    <p>обеспечить учет и анализ данных медицинского обследования водителей;</p>

    <p>проводить инструктажи водителей по безопасности дорожного движения (вводный, предрейсовый, сезонный, специальный) и проверку навыков практического вождения транспортных средств;</p>

    <p>установить постоянный контроль за выполнением работниками возложенных на них обязанностей по обеспечению безопасности автомобильных перевозок пассажиров;</p>

    <p>координировать работу своих служб (отделов, работников) по обеспечению безопасности дорожного движения и автомобильных перевозок пассажиров.</p>

    <p>235. В случае неисполнения либо ненадлежащего исполнения автомобильным перевозчиком обязанностей по обеспечению безопасности автомобильных перевозок пассажиров он несет ответственность в соответствии с действующим законодательством.</p>

    <p>236. Контроль за выполнением требований по обеспечению безопасности автомобильных перевозок пассажиров осуществляется уполномоченными лицами автомобильных перевозчиков и других юридических лиц, оказывающих услуги по автомобильной перевозке пассажиров, а также уполномоченными лицами соответствующих республиканских органов государственного управления, местных исполнительных и распорядительных органов в пределах их компетенции.</p>

    <p></p>

    <p align="center">Глава 17</p>

    <p align="center">ОТВЕТСТВЕННОСТЬ АВТОМОБИЛЬНОГО ПЕРЕВОЗЧИКА, ЗАКАЗЧИКА</p>

    <p align="center">АВТОМОБИЛЬНЫХ ПЕРЕВОЗОК ПАССАЖИРОВ, ПАССАЖИРА, ОПЕРАТОРА</p>

    <p align="center">АВТОМОБИЛЬНЫХ ПЕРЕВОЗОК ПАССАЖИРОВ</p>

    <p></p>

    <p>237. За неисполнение или ненадлежащее исполнение обязательств по договору об автомобильной перевозке пассажиров, договору о фрахтовании для автомобильных перевозок пассажиров, договору об организации автомобильных перевозок пассажиров стороны несут ответственность, установленную актами законодательства, а также соглашением сторон.</p>

    <p>Соглашения сторон об ограничении или устранении установленной законодательством ответственности недействительны, за исключением случаев, когда возможность таких соглашений предусмотрена законодательством.</p>

    <p>238. Автомобильный перевозчик несет имущественную и другую ответственность в соответствии с законодательством за причинение вреда жизни или здоровью пассажира, если это связано с выполнением автомобильной перевозки и имело место либо во время нахождения пассажира в салоне транспортного средства, либо во время посадки или высадки, либо в связи со сдачей или приемом багажа.</p>

    <p>239. Автомобильный перевозчик несет ответственность как за свои действия, так и за действия третьих лиц, к услугам которых он прибегает для выполнения обязательств, возлагаемых на него в соответствии с договором об автомобильной перевозке пассажиров, когда эти лица действуют в пределах своих обязательств. При этом ссылки автомобильного перевозчика на физические или психические недостатки водителя, а также на неисправность транспортного средства не являются основанием для освобождения его от ответственности.</p>

    <p>240. За неподачу или несвоевременную подачу транспортного средства в соответствии с соответствующим договором автомобильный перевозчик несет ответственность перед заказчиком в размере 20 процентов от стоимости перевозки, если иное не предусмотрено соглашением сторон. При этом обязательно предъявление заказчиком претензии автомобильному перевозчику не позднее 30 календарных дней с даты подачи транспортного средства, установленной соответствующим договором.</p>

    <p>Автомобильный перевозчик освобождается от ответственности за неподачу или несвоевременную подачу транспортного средства, если это произошло вследствие непреодолимой силы или других непредотвратимых при данных условиях обстоятельств, прекращения или ограничения автомобильных перевозок пассажиров в определенном направлении в установленном порядке, а также в иных случаях, предусмотренных законодательством.</p>

    <p>241. Автомобильный перевозчик несет ответственность за несохранность (утрату, недостачу, повреждение) багажа, принятого к автомобильной перевозке, если не докажет, что несохранность багажа вызвана обстоятельствами, которые он не мог предотвратить и устранение которых от него не зависело.</p>

    <p>Ущерб, причиненный при автомобильной перевозке багажа и (или) ручной клади, возмещается автомобильным перевозчиком при:</p>

    <p>утрате или недостаче багажа - в размере стоимости утраченного или недостающего багажа;</p>

    <p>повреждении (порче) багажа или ручной клади по вине автомобильного перевозчика - в размере суммы, на которую снизилась стоимость, а при невозможности восстановления - в размере стоимости;</p>

    <p>утрате багажа, сданного к автомобильной перевозке с объявлением его ценности, - в размере объявленной ценности багажа.</p>

    <p>Если недостача или повреждение (порча) части багажа влияет на стоимость другой части багажа по данной багажной квитанции, то при определении размера возмещения учитываются обе части этого багажа.</p>

    <p>Автомобильный перевозчик наряду с возмещением установленного ущерба, вызванного утратой, недостачей или повреждением (порчей) багажа, возвращает пассажиру полученную от него плату за провоз багажа.</p>

    <p>Стоимость багажа определяется исходя из его цены, указанной в счете продавца или предусмотренной в соответствующем договоре, а при отсутствии счета или указания цены в договоре - исходя из цены на аналогичные товары при сравнимых обстоятельствах.</p>

    <p>242. В случае отмены или переноса рейса автомобильной перевозки пассажиров в регулярном сообщении автомобильный перевозчик или оператор автомобильных перевозок пассажиров по требованию пассажира до начала отправления автобуса возвращает пассажиру стоимость предъявленного разового билета, а также стоимость оплаченных пассажиром дополнительных услуг по предстоящей перевозке.</p>

    <p>243. При международной, если иное не предусмотрено договором об автомобильной перевозке пассажиров, и междугородной перевозках в регулярном сообщении за задержку отправления автобуса или опоздание прибытия в пункт назначения более чем на 1 час автомобильный перевозчик уплачивает пассажиру неустойку в размере 20 процентов от стоимости автомобильной перевозки пассажира, если не докажет, что задержка или опоздание имели место вследствие непреодолимой силы, устранения неисправностей автобуса, угрожающих жизни и здоровью пассажиров, или иных обстоятельств, не зависящих от перевозчика. В случае отказа пассажира от автомобильной перевозки по причине такой задержки автобуса автомобильный перевозчик или оператор автомобильных перевозок пассажиров обязан возвратить пассажиру провозную плату в полном объеме.</p>

    <p>244. Вред, причиненный автомобильным перевозчиком жизни или здоровью пассажира, подлежит возмещению в соответствии с главой 58 Гражданского кодекса Республики Беларусь, если законодательством или договором об автомобильной перевозке пассажиров не предусмотрена повышенная ответственность автомобильного перевозчика.</p>

    <p>245. Заказчик автомобильных перевозок пассажиров в нерегулярном сообщении возмещает автомобильному перевозчику убытки, причиненные в связи с неиспользованием или несвоевременным использованием поданных в срок транспортных средств в размере затрат на подачу этих средств, если иное не предусмотрено законодательством или соглашением сторон, а также уплачивает неустойку на основе предъявления автомобильным перевозчиком претензии не позднее 30 календарных дней со дня подачи транспортного средства. Размер неустойки устанавливается соглашением сторон.</p>

    <p>Заказчик автомобильной перевозки пассажиров в нерегулярном сообщении освобождается от ответственности, указанной в части первой настоящего пункта, если неиспользование или несвоевременное использование поданных в срок автомобильным перевозчиком транспортных средств произошло вследствие непреодолимой силы, иных явлений стихийного характера (пожаров, заносов, наводнений) или военных действий, прекращения или ограничения автомобильных перевозок в определенных направлениях в установленном порядке, а также в иных случаях, предусмотренных законодательством.</p>

    <p>246. Заказчик автомобильных перевозок пассажиров обязан возместить ущерб, причиненный по его вине имуществу автомобильного перевозчика, а также имуществу третьих лиц, за которое несет ответственность перевозчик. Размер возмещения определяется величиной причиненного ущерба.</p>

    <p>Ответственность за иные отклонения от условий договора об автомобильной перевозке пассажиров, которые привели к дополнительным расходам автомобильного перевозчика, определяется соглашением сторон и законодательством.</p>

    <p>247. При проезде в автобусе без билета (с недействительным билетом, в том числе не погашенным с помощью устройства для гашения) или провозе багажа и (или) ручной клади без оплаты пассажир должен оплатить проезд или провоз багажа и (или) ручной клади и уплатить штраф в соответствии с законодательством.</p>

    <p>248. При проезде пассажира в автобусе без соответствующих документов, подтверждающих право на льготы по оплате проезда на автомобильном транспорте, пассажир должен оплатить полную стоимость проезда и уплатить штраф в соответствии с действующим законодательством.</p>

    <p>249. В случае нарушения пассажиром норм настоящих Правил и отказа от уплаты штрафа, а также невозможности установления его личности из-за непредъявления соответствующих документов контролирующее лицо должно обратиться в органы внутренних дел для установления личности и оформления протокола об административном правонарушении.</p>

    <p>250. Случаи исключения и ограничения ответственности, установленные гражданским законодательством и настоящими Правилами для заказчиков и автомобильных перевозчиков, распространяются также на операторов автомобильных перевозок пассажиров, представляющих интересы заказчиков и автомобильных перевозчиков.</p>

