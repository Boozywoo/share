    <p>116. Билеты на междугородные перевозки пассажиров в регулярном сообщении и багажные квитанции на провоз багажа должны оформляться в билетных кассах, расположенных на пассажирских терминалах, остановочных пунктах и в других установленных местах, а при отсутствии билетных касс - водителем автобуса.</p>

    <p>Продажа бланочных билетов отмечается в билетно-учетном листе.</p>

    <p>Продажа билетов на междугородные автомобильные перевозки пассажиров в регулярном сообщении прекращается в кассе начального остановочного пункта маршрута за 5 минут до отправления автобуса, а в кассе промежуточного остановочного пункта маршрута - непосредственно перед отправлением автобуса.</p>

    <p>117. Предварительная продажа билетов и бронирование места для проезда при междугородных автомобильных перевозках пассажиров в регулярном сообщении производятся не позднее чем за 15 суток и заканчиваются в день, предшествующий дню отправления автобуса.</p>

    <p>118. Бронирование мест для проезда при междугородных автомобильных перевозках пассажиров в регулярном сообщении может производиться телеграммой с уведомлением, а также по телефону с возможностью установления обратной телефонной связи или по сети Интернет.</p>

    <p>Бронирование места для проезда производится при наличии свободных мест. В заказе должны указываться заказчик, дата, время и пункт отправления, пункт назначения, число билетов, а также номер телефона заказчика (при бронировании по телефону обязательно).</p>

    <p>119. В случае, если билеты на забронированные места для проезда не востребованы за один час до отправления автобуса, бронирование аннулируется.</p>

    <p>120. Билет на льготный проезд при междугородной автомобильной перевозке пассажиров в регулярном сообщении транспортом общего пользования оформляется лицу, предъявившему документ, подтверждающий право на льготы по оплате проезда на автомобильном транспорте.</p>

    <p>121. Посадка пассажиров в автобус при междугородной автомобильной перевозке пассажиров в регулярном сообщении должна производиться в порядке очереди через переднюю дверь для пассажиров, а высадка - через все двери для пассажиров.</p>

