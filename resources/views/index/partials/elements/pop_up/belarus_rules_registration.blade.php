    <p>257. Контроль за соблюдением законодательства об автомобильном транспорте и автомобильных перевозках пассажиров, международных договоров Республики Беларусь в области международных автомобильных перевозок пассажиров и их багажа осуществляется Министерством транспорта и коммуникаций, местными исполнительными и распорядительными органами и другими государственными органами в пределах их компетенции.</p>

    <p>258. В случаях выявления нарушений лицензионных требований выполнения автомобильных перевозок пассажиров уполномоченные государственные органы и организации принимают меры в соответствии с законодательством.</p>

    <p>259. Решения контролирующих лиц в отношении пассажиров, автомобильных перевозчиков и операторов автомобильных перевозок пассажиров могут быть обжалованы в порядке, установленном законодательством.</p>

