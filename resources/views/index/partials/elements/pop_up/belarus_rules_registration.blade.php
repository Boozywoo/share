    <p>Заключение договора о фрахтовании для автомобильной перевозки пассажиров подтверждается оформлением заказ-наряда.</p>

    <p>Заключение и исполнение договора о перевозке пассажиров автомобилем-такси удостоверяется кассовым чеком, оформляемым с применением таксометра.</p>

    <p>Договор об автомобильной перевозке пассажиров в регулярном сообщении и договор о перевозке автомобилями-такси являются публичными.</p>

    <p>30. К выполнению автомобильных перевозок пассажиров могут допускаться автомобильные перевозчики, имеющие:</p>

    <p>специальное разрешение (лицензию) на осуществление деятельности в области автомобильного, внутреннего водного, морского транспорта (исключая внутриреспубликанские перевозки для собственных нужд);</p>

    <p>транспортные средства на праве собственности, хозяйственного ведения, аренды и иных законных основаниях, а также условия для их хранения, проведения технического обслуживания, контроля технического состояния;</p>

    <p>условия для проведения медицинского обследования водителей.</p>

    <p>Кроме перечисленного в части первой настоящего пункта, автомобильный перевозчик должен назначить лиц, ответственных за организацию и выполнение автомобильных перевозок, и нанять водителей в соответствии с требованиями, установленными настоящими Правилами и иными нормативными правовыми актами.</p>

    <p>31. Подача транспортного средства заказчику автомобильных перевозок пассажиров производится в порядке, определенном в статье 34 Закона Республики Беларусь "Об автомобильном транспорте и автомобильных перевозках".</p>

    <p>32. Автомобильные перевозчики должны обеспечивать безопасную, своевременную и качественную перевозку пассажиров и их багажа и (или) ручной клади.</p>

    <p>У автомобильного перевозчика должна быть книга замечаний и предложений.</p>

    <p>Автомобильный перевозчик должен осуществлять анализ качества предоставления услуг, а также вести учет обращений, предложений и замечаний.</p>

    <p>33. Организация городских, пригородных и междугородных внутриобластных автомобильных перевозок пассажиров в регулярном сообщении осуществляется местными исполнительными и распорядительными органами, являющимися заказчиками автомобильных перевозок пассажиров в регулярном сообщении (далее - заказчики автомобильных перевозок пассажиров в регулярном сообщении), либо по их решению операторами автомобильных перевозок пассажиров.</p>

    <p>Организация междугородных межобластных автомобильных перевозок пассажиров в регулярном сообщении осуществляется Министерством транспорта и коммуникаций либо по его решению операторами автомобильных перевозок пассажиров.</p>

    <p>34. При организации внутриреспубликанских автомобильных перевозок пассажиров в регулярном сообщении совместным решением операторов пассажирских терминалов по согласованию с автомобильными перевозчиками могут вводиться дополнительные рейсы.</p>

    <p>35. Городские, пригородные и междугородные автомобильные перевозки пассажиров в регулярном сообщении выполняются в соответствии с договором об организации автомобильных перевозок пассажиров в регулярном сообщении по примерной форме, утверждаемой Министерством транспорта и коммуникаций, заключаемым с заказчиком либо оператором автомобильных перевозок пассажиров по результатам конкурса на право выполнения автомобильных перевозок пассажиров в регулярном сообщении либо в соответствии с решением заказчика или оператора в случаях, предусмотренных законодательством.</p>

    <p>Указанный договор может заключаться на срок до 5 лет.</p>

    <p>Министерством транспорта и коммуникаций создается постоянно действующая комиссия для принятия решений о предоставлении права на выполнение международных автомобильных перевозок пассажиров в регулярном сообщении.</p>

    <p>Выдача разрешений на выполнение международных автомобильных перевозок пассажиров автобусами в регулярном сообщении по форме, утверждаемой Министерством транспорта и коммуникаций, осуществляется на основании указанных решений. Оформление карточек маршрута и данных разрешений может производиться уполномоченной Министерством транспорта и коммуникаций организацией.</p>

    <p>Право на выполнение внутриреспубликанских автомобильных перевозок пассажиров в регулярном сообщении предоставляется автомобильному перевозчику на срок до 5 лет и подтверждается карточкой маршрута, которая выдается заказчиком или оператором автомобильных перевозок пассажиров в регулярном сообщении либо Министерством транспорта и коммуникаций на каждый автобус, используемый при выполнении таких перевозок.</p>

    <p>Междугородная автомобильная перевозка пассажиров в регулярном сообщении должна быть начата не позднее чем через 45 суток со дня выдачи автомобильному перевозчику карточки маршрута.</p>

    <p>Разрешения на выполнение международных автомобильных перевозок пассажиров в регулярном сообщении выдаются, как правило, сроком на 5 лет.</p>

    <p>Автомобильные перевозки пассажиров в регулярном сообщении без карточки маршрута или соответствующего разрешения запрещаются.</p>

    <p>36. Каждому маршруту внутриреспубликанских автомобильных перевозок пассажиров в регулярном сообщении присваивается номер с указанием вида сообщения символами: "С" - для скоростного, "Э" - для экспрессного (за исключением перевозок автобусами категории М2), "Т" - для экспрессного при перевозках автобусами категории М2 (маршрутное такси), "Д" - для дополнительного маршрута. Номер маршрута при автомобильных перевозках пассажиров в регулярном сообщении, которые не относятся к перевозкам транспортом общего пользования (иные автомобильные перевозки пассажиров в регулярном сообщении), дополняется символом "К". Не допускается присвоение одинаковых номеров маршрутов в одном городе (населенном пункте), а также прохождение маршрутов пригородных и междугородных автомобильных перевозок пассажиров с одинаковыми номерами через один и тот же остановочный пункт.</p>

    <p>37. Автомобильные перевозки пассажиров в регулярном сообщении должны обеспечивать потребности населения в быстром и безопасном перемещении с минимальным числом пересадок, в том числе с одного на другой вид транспорта.</p>

    <p>При необходимости выполнения перевозок пассажиров автобусами к местам проведения праздников, спортивных, культурно-зрелищных и других массовых мероприятий, а также в случаях приостановки перевозок пассажиров отдельными автомобильными перевозчиками или перевозок другими видами транспорта, заказчиком автомобильных перевозок пассажиров в регулярном сообщении либо уполномоченным им оператором могут вводиться временные маршруты перевозок пассажиров в регулярном сообщении.</p>

    <p>38. Автомобильная перевозка пассажиров выполняется по маршруту, определяемому в зависимости от вида автомобильной перевозки в соответствии с требованиями статьи 36 Закона Республики Беларусь "Об автомобильном транспорте и автомобильных перевозках".</p>

    <p>39. На каждый маршрут городских, пригородных или междугородных автомобильных перевозок пассажиров в регулярном сообщении до начала их выполнения должен быть оформлен паспорт маршрута автомобильных перевозок пассажиров в регулярном сообщении (далее - паспорт маршрута) по форме, утверждаемой Министерством транспорта и коммуникаций.</p>

    <p>40. Разработка паспорта маршрута осуществляется заказчиком, оператором автомобильных перевозок пассажиров или автомобильным перевозчиком и производится ими самими либо иной организацией по их заказу. Паспорт маршрута подписывается разработчиком и согласовывается с соответствующим подразделением Государственной автомобильной инспекции Министерства внутренних дел (далее - подразделение Государственной автомобильной инспекции). Паспорт маршрута городских, пригородных и междугородных внутриобластных автомобильных перевозок пассажиров в регулярном сообщении утверждается заказчиком или оператором автомобильных перевозок пассажиров, а междугородных межобластных - Министерством транспорта и коммуникаций. Изменения характеристик маршрута должны быть зарегистрированы соответственно заказчиком или оператором автомобильных перевозок пассажиров либо Министерством транспорта и коммуникаций.</p>

    <p>41. Паспорт маршрута заполняется в двух экземплярах. Первый экземпляр паспорта маршрута хранится в организации, его утвердившей, второй - у разработчика. У автомобильного перевозчика должна быть копия паспорта маршрута, заверенная организацией, его утвердившей.</p>

    <p>42. Расписание движения автобусов по маршруту автомобильных перевозок пассажиров в регулярном сообщении должно быть соотнесено с расписанием движения пассажирских транспортных средств на дублирующих маршрутах, в том числе других видов транспорта, и составляться на основании следующих данных:</p>

    <p>объем пассажиропотоков по направлениям и часам суток, дням недели, периодам года;</p>

    <p>характеристика дорожных условий на маршруте;</p>

    <p>безопасные скорости движения;</p>

    <p>провозные возможности транспортных средств;</p>

    <p>требования к режиму труда и отдыха водителей и другие.</p>

    <p>43. Расписание движения автобусов по маршруту автомобильных перевозок пассажиров в регулярном сообщении составляется автомобильным перевозчиком либо оператором и утверждается оператором или заказчиком.</p>

    <p>44. Полное расписание движения автобусов по маршруту при перевозках пассажиров в регулярном сообщении должно содержать:</p>

    <p>наименование и номер маршрута;</p>

    <p>количество рейсов по маршруту, протяженность и длительность выполнения рейса в прямом и обратном направлениях;</p>

    <p>время выезда автобуса с места стоянки и прибытия на начальный остановочный пункт;</p>

    <p>время начала работы автобуса на маршруте;</p>

    <p>время прохождения автобусом пунктов маршрута по рейсам;</p>

    <p>время и место перерыва в работе водителей и межрейсового отстоя автобуса;</p>

    <p>время и место смены экипажа автобуса;</p>

    <p>время завершения работы на маршруте и время возвращения автобуса к месту стоянки.</p>

    <p>45. Расписание движения автобусов, приводимое на остановочном пункте при городских автомобильных перевозках пассажиров в регулярном сообщении, должно содержать:</p>

    <p>номер маршрута и его конечные пункты;</p>

    <p>время прохождения автобуса при выполнении первого и последнего рейса по рабочим, выходным и праздничным дням;</p>

    <p>интервал движения автобусов (если интервал движения не более 15 минут) или время движения по рейсам (если интервал движения более 15 минут) по периодам суток для рабочих, выходных и праздничных дней.</p>

    <p>Расписание движения автобусов, приводимое на остановочном пункте при пригородных и междугородных автомобильных перевозках пассажиров в регулярном сообщении, должно содержать:</p>

    <p>наименование остановочного пункта;</p>

    <p>номер маршрута, его начальный и конечный пункты;</p>

    <p>время прохождения автобуса при выполнении каждого рейса по рабочим, выходным и праздничным дням.</p>

    <p>46. Остановочный пункт должен быть обозначен дорожными знаками в соответствии с действующими техническими нормативными правовыми актами.</p>

    <p>47. При выполнении городских автомобильных перевозок пассажиров в регулярном экспрессном сообщении автобусами категории М2, а также междугородных автомобильных перевозок пассажиров в регулярном сообщении (при наличии на маршруте остановочных пунктов, на которых отсутствуют пассажирские терминалы) используются билеты, оформляемые с применением кассовых суммирующих аппаратов транспорта.</p>

    <p>В случае технической неисправности кассового суммирующего аппарата транспорта, возникшей при выполнении перевозки пассажиров, и на время ремонта данного аппарата допускается использование бланочных билетов установленной формы, информация об изготовлении и реализации которых включена в электронный банк данных об изготовленных и реализованных бланках первичных учетных документов и контрольных знаках.</p>

    <p>Формы бланков билетов и багажных квитанций на городские, пригородные и междугородные внутриобластные автомобильные перевозки пассажиров в регулярном сообщении устанавливаются заказчиками автомобильных перевозок пассажиров в регулярном сообщении, а на междугородные межобластные и международные перевозки пассажиров в регулярном сообщении - Министерством транспорта и коммуникаций. При международных автомобильных перевозках пассажиров в регулярном сообщении могут применяться бланки билетов, установленные в других государствах.</p>

    <p>48. При выполнении автомобильных перевозок пассажиров в регулярном экспрессном сообщении автобусами категории М2 посадка-высадка пассажиров производятся только на остановочных пунктах.</p>

    <p>49. Заказчик автомобильных перевозок пассажиров в регулярном сообщении обязан обеспечивать:</p>

    <p>обустройство, оформление и содержание пассажирских терминалов, остановочных пунктов маршрутов автомобильных перевозок пассажиров в регулярном сообщении, в том числе входящих в их состав объектов санитарно-бытового обслуживания водителей и пассажиров;</p>

    <p>содержание дорог и улиц городов (населенных пунктов), по территории которых проходит маршрут, в состоянии, пригодном для выполнения автомобильных перевозок пассажиров.</p>

    <p>50. Оператор автомобильных перевозок пассажиров в регулярном сообщении или по его поручению автомобильный перевозчик обязан:</p>

    <p>не позднее 15 суток до даты открытия, изменения или закрытия маршрута перевозок пассажиров в регулярном сообщении информировать об этом население через средства массовой информации, путем внесения изменений в расписание движения автобусов или иными способами;</p>

    <p>предварительно, но не позднее чем за один день, информировать население о предстоящем изменении стоимости проезда на маршрутах перевозок пассажиров в регулярном сообщении.</p>

    <p>51. Во время автомобильной перевозки в регулярном сообщении пассажир обязан иметь билет и (или) документ, подтверждающий право на льготы по оплате проезда на автомобильном транспорте. При автомобильной перевозке багажа и ручной клади на платной основе пассажир должен иметь багажную квитанцию, подтверждающую оплату их провоза. Автомобильный перевозчик или оператор автомобильных перевозок пассажиров обязан обеспечить продажу пассажиру билета для данного вида автомобильных перевозок и при необходимости оформить багажную квитанцию на перевозку багажа и оплату провоза ручной клади.</p>

    <p>52. Реализация билетов на автомобильные перевозки пассажиров в регулярном сообщении должна производиться в порядке очереди. Приобретать билеты вне очереди в кассе или в салоне автобуса могут лица, которым это право предоставлено в соответствии с законодательством.</p>

    <p>53. При пригородных и междугородных автомобильных перевозках пассажиров в регулярном сообщении для совершения поездки пассажира, имеющего право на льготы по оплате проезда на автомобильном транспорте, оформляется льготный билет.</p>

    <p>Льготные билеты при автомобильных перевозках пассажиров автобусами в регулярном сообщении реализуются гражданам, предъявившим документ установленного образца, подтверждающий предоставление им в соответствии с законами и актами Президента Республики Беларусь права на льготы по оплате проезда на автомобильном транспорте.</p>

    <p>54. Граждане, имеющие право на льготы по оплате проезда на автомобильном транспорте, на общих основаниях оплачивают предварительную продажу билета и (или) бронирование места для проезда и доставку билета на дом, а также провоз багажа и ручной клади сверх нормы бесплатного провоза.</p>

    <p>55. Билет на одну поездку с указанием времени отправления автобуса в рейс является действительным только на поездку по указанному в нем времени отправления. В случае утери билета пассажир не может претендовать на совершение поездки и возврат уплаченных за поездку средств (кроме международных перевозок пассажиров, на которые может быть оформлен дубликат билета).</p>

    <p>В случаях отсутствия у пассажира билета, а также документа, подтверждающего право на льготы по оплате проезда на автомобильном транспорте (при наличии льготного билета), пассажир считается безбилетным и несет ответственность в соответствии с законодательством.</p>

    <p>56. Лицо, имеющее билет на одну пригородную или междугородную поездку в регулярном сообщении, может не позднее чем за два часа до отправления автобуса возвратить билет в кассу и получить обратно уплаченную стоимость проезда. Плата за предварительную продажу билета и (или) бронирование места для проезда, а также за доставку билета на дом не возвращается.</p>

    <p>57. При возврате в кассу билета менее чем за два часа, но не менее чем за 15 минут до отправления автобуса, лицу, сдающему билет, возвращается стоимость проезда за вычетом 15 процентов.</p>

    <p>58. Если гражданин, имеющий билет на одну пригородную или междугородную поездку в регулярном сообщении, опоздал ко времени отправления автобуса или отказался от поездки меньше чем за 15 минут до отправления, он имеет право в течение 2 часов, а в случае болезни или несчастного случая в течение 3 суток со времени отправления автобуса в рейс переоформить билет на следующий рейс того же маршрута или направления с доплатой 25 процентов стоимости проезда или получить уплаченную стоимость проезда за вычетом 25 процентов.</p>

    <p>59. При прекращении междугородной автомобильной перевозки пассажира вследствие несчастного случая пассажир имеет право на возврат суммы, уплаченной им за перевозку, за вычетом стоимости проделанного пути.</p>

    <p>60. Если автомобильный перевозчик приостановил перевозку пассажира по техническим или другим причинам, то по выбору пассажира он должен в возможно короткий срок обеспечить доставку пассажира другим рейсом в начальный или конечный пункт его поездки либо до ближайшего пассажирского терминала. При городских перевозках пассажиров в регулярном сообщении билеты на проезд действительны на другой рейс этого маршрута или направления, выполняемый автомобильным перевозчиком. В случае доставки пассажира автомобильным перевозчиком в начальный пункт следования или необеспечения им доставки пассажира в пункт назначения, пассажир имеет право на возврат суммы, уплаченной им за поездку, в полном объеме. В случае доставки пассажира автомобильным перевозчиком до ближайшего пассажирского терминала пассажиру возвращается сумма, уплаченная им за перевозку, за исключением стоимости выполненной перевозки этого пассажира.</p>

    <p>61. Международные автомобильные перевозки пассажиров и их багажа и (или) ручной клади выполняются автомобильными перевозчиками в соответствии с законодательством Республики Беларусь и законодательством государств, по которым проходит маршрут.</p>

    <p>62. При выполнении международных автомобильных перевозок пассажиров документы, предъявляемые экипажем автобуса и пассажирами для пограничного и таможенного контроля, а также багаж, ручная кладь и валюта должны быть оформлены в соответствии с требованиями законодательства государств, по территории которых будет выполняться перевозка.</p>

