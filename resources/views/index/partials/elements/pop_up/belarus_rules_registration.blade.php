    <p>183. Автомобильные перевозчики, выполняющие автомобильные перевозки групп детей автобусами в нерегулярном сообщении, либо заказчик этих перевозок обязаны заблаговременно, но не позднее трех суток до начала перевозки, представить в подразделения Государственной автомобильной инспекции (по месту отправления групп детей) официальное уведомление о планируемой перевозке. В данном уведомлении указываются дата и маршрут движения, марка и номер государственной регистрации автобуса (автобусов), фамилии и инициалы водителей, которые будут выполнять перевозку детей, с приложением копий списков детей и лиц, их сопровождающих, утвержденных заказчиком.</p>

    <p>(в ред. постановления Совмина от 20.04.2009 N 499)</p>

    <p>(см. текст в предыдущей редакции)</p>

    <p>(часть исключена. - Постановление Совмина от 20.04.2009 N 499)</p>

    <p>(см. текст в предыдущей редакции)</p>

    <p>184. После получения уведомления, указанного в пункте 183 настоящих Правил, сотрудники соответствующего подразделения Государственной автомобильной инспекции обязаны проинструктировать водителей об особенностях требований Правил дорожного движения при автомобильных перевозках групп детей, а также мерах предосторожности, обеспечивающих безопасность таких перевозок по маршруту.</p>

    <p>Инструктаж водителей иностранных автомобильных перевозчиков, не владеющих русским языком, проводится с участием переводчика, который приглашается организацией - организатором перевозки. Информация об инструктаже водителей заносится под роспись каждого из инструктируемых в специальный журнал, который хранится в соответствующем подразделении Государственной автомобильной инспекции.</p>

    <p>185. Технический осмотр автобусов, в том числе с иностранной регистрацией, предусмотренных для автомобильной перевозки групп детей в нерегулярном сообщении, проводится не ранее чем за 7 дней до начала выполнения перевозки на технической базе автомобильных перевозчиков либо в первоочередном порядке на диагностических станциях. По результатам технического осмотра составляется акт технического осмотра автобуса или диагностическая карта в двух экземплярах с заключением о его допуске к перевозке групп детей. Акт технического осмотра или диагностическая карта подписывается членами комиссии и заверяется печатью организации по месту составления. Первый экземпляр акта технического осмотра или диагностической карты передается водителю автобуса, находится в автобусе до окончания поездки, а затем хранится у автомобильного перевозчика в течение 12 месяцев, второй - в организации, проводившей технический осмотр, в течение 10 дней после возвращения группы детей из поездки.</p>

    <p>186. В состав специально созданных комиссий по проведению технических осмотров автобусов, как правило, должны включаться начальник и другие работники службы (отдела) технического контроля, начальник и другие работники службы (отдела) безопасности движения, работники службы (отдела) эксплуатации, другие лица, ответственные за организацию технического обслуживания и ремонта автобусов.</p>

    <p>187. К автомобильной перевозке автобусами групп детей допускаются водители, имеющие не менее трех лет непрерывного стажа работы на механических транспортных средствах категории "D" и не привлекавшиеся на протяжении последних двух лет к административной ответственности за нарушения Правил дорожного движения, о чем в соответствующие подразделения Государственной автомобильной инспекции должно быть представлено письменное подтверждение, заверенное подписью руководителя и печатью организации (в том числе и иностранной), предоставившей водителей для перевозки групп детей.</p>

    <p>188. Для автомобильной перевозки автобусами групп детей на расстояние более 450 км на каждый автобус, перевозящий детей, выделяется по два водителя.</p>

    <p>189. Автомобильным перевозчикам, выполняющим автомобильные перевозки групп детей автобусами, запрещается допускать к поездке водителей, отдых которых между сменами был менее 12 часов.</p>

    <p>190. Заказчик автомобильной перевозки групп детей на каждый автобус назначает совершеннолетнего сопровождающего, а если число перевозимых детей более 20 - двух совершеннолетних сопровождающих. Сопровождающие обязаны принимать меры, обеспечивающие безопасную перевозку групп детей автобусами. Из числа сопровождающих назначается старший, который должен находиться в головном автобусе и следить за выполнением требований настоящих Правил в ходе перевозки.</p>

    <p>191. Общее количество перевозимых в автобусах детей и взрослых не должно превышать числа мест для сидения в соответствии с технической характеристикой данного автобуса.</p>

    <p>192. Водители автобусов, в том числе автомобильных перевозчиков иностранных государств, выполняющие перевозки групп детей, обязаны соблюдать Правила дорожного движения и требования настоящих Правил, а также установленный режим труда и отдыха.</p>

    <p>193. При возникновении в пути следования технических неисправностей автобуса, угрожающих безопасности движения, а также при ухудшении состояния здоровья водителей запрещается дальнейшее движение до устранения обстоятельств, препятствующих безопасному движению.</p>

    <p>194. При автомобильных перевозках групп детей автобусами по территории Республики Беларусь скорость движения автобусов, включая автобусы с мягкими сиденьями, не должна превышать 70 км/ч. На автобусах, используемых при выполнении перевозки групп детей (одиночных или следующих колонной), в соответствии с требованиями Правил дорожного движения независимо от времени суток должен быть включен ближний свет фар или дневные ходовые огни (при их наличии) и установлены опознавательные знаки "Перевозка детей".</p>

    <p>195. Автомобильная перевозка групп детей автобусами с 23.00 до 05.00 часов, а также в условиях недостаточной видимости (туман, дождь, снегопад и тому подобное) не рекомендуется. С 23.00 до 05.00 часов в порядке исключения допускается перевозка детей к железнодорожным вокзалам и аэропортам и от них, а также при задержках в пути до ближайшего места ночлега.</p>

    <p>196. При получении информации об автомобильной перевозке групп детей автобусами в составе организованной транспортной колонны, включающей от 3 до 10 автобусов, подразделения Государственной автомобильной инспекции обязаны обеспечить их сопровождение одним транспортным средством оперативного назначения, а колонны, включающей свыше 10 автобусов, - двумя транспортными средствами оперативного назначения.</p>

    <p>197. Сопровождение организованной транспортной колонны автобусов при автомобильной перевозке групп детей осуществляется от места ее формирования до конечного пункта назначения в пределах Республики Беларусь.</p>

    <p>198. Если автомобильная перевозка детей выполняется одним или двумя автобусами, сопровождение транспортными средствами оперативного назначения Государственной автомобильной инспекции не требуется.</p>

    <p>199. В случаях выявления нарушений требований настоящих Правил или Правил дорожного движения дальнейшая автомобильная перевозка групп детей автобусами до принятия мер по устранению нарушений и обстоятельств, препятствующих дальнейшему безопасному движению, запрещается.</p>

    <p>200. Положения настоящей главы применяются при выполнении автомобильных перевозок автобусами групп детей до 16 лет общей численностью 8 и более человек. В составе группы детей не учитываются дети, перевозка каждого из которых осуществляется в сопровождении родителя (родителей).</p>

    <p></p>

    <p align="center">Глава 12</p>

    <p align="center">ПОРЯДОК ОРГАНИЗАЦИИ РАБОТЫ ПАССАЖИРСКИХ ТЕРМИНАЛОВ</p>

    <p></p>

    <p>201. Обустройство и информационное обеспечение пассажирских терминалов и остановочных пунктов на них должно соответствовать требованиям технических нормативных правовых актов.</p>

    <p>202. Оказание услуг на пассажирском терминале автомобильному перевозчику производится согласно соответствующим договорам между оператором терминала и автомобильным перевозчиком.</p>

    <p>203. На пассажирском терминале оказываются следующие виды услуг: реализация билетов, управление прибытием и отправлением автобусов, контроль экипировки и санитарного состояния автобусов, бытовое обслуживание пассажиров, водителей, кондукторов и другие.</p>

    <p>204. На пассажирском терминале, обеспечивающем пригородные, междугородные или международные автомобильные перевозки пассажиров, должны иметься:</p>

    <p>надпись с наименованием пассажирского терминала;</p>

    <p>указатели с номерами на платформах, а при необходимости - с названиями конечных пунктов маршрутов или направлениями;</p>

    <p>схема размещения помещений и служб;</p>

    <p>расписание отправления и прибытия автобусов с указанием номера платформы;</p>

    <p>таблица расстояний и тарифы на проезд;</p>

    <p>перечень категорий граждан, которые имеют право на льготы по оплате проезда на автомобильном транспорте или внеочередное приобретение билетов;</p>

    <p>схемы размещения пассажирских мест в автобусах;</p>

    <p>перечень оказываемых пассажирам услуг;</p>

    <p>информация о номерах телефонов, по которым осуществляется бронирование мест для проезда;</p>

    <p>режим работы пассажирского терминала и билетных касс;</p>

    <p>схема безопасного движения пешеходов;</p>

    <p>телефоны должностных лиц, к которым могут обратиться пассажиры в случае возникновения спорных ситуаций в процессе их обслуживания.</p>

    <p>205. Гражданам, находящимся на пассажирском терминале, через громкоговорящую связь на государственных языках Республики Беларусь или на одном из них сообщается информация:</p>

    <p>о несвоевременном отправлении или прибытии автобусов - непосредственно после получения такой информации с последующим повторением через каждые 15 минут;</p>

    <p>об отмене рейса или замене автобуса на автобус другой марки - за 1,5 часа до времени отправления, установленного расписанием движения, или как только об этом поступила информация, и повторяется через каждые 15 минут;</p>

    <p>о введении дополнительного рейса или об изменении порядка работы билетных касс - непосредственно после получения информации и повторяется через каждые 15 минут.</p>

    <p>206. Книга замечаний и предложений должна находиться у администратора пассажирского терминала, диспетчера или кассира билетной кассы.</p>

    <p>207. Работа билетных касс на пассажирском терминале должна обеспечивать минимальные затраты времени пассажиров на приобретение билетов и начинаться за 30 минут до начала выполнения первого рейса с этого пассажирского терминала, а заканчиваться не ранее времени отправления последнего рейса.</p>

    <p>208. Текущая продажа билетов на рейсы, на которые объявлена посадка, должна производиться в билетных кассах пассажирского терминала в первоочередном порядке.</p>

    <p>209. В местах расположения билетных касс должна размещаться следующая информация:</p>

    <p>наименование билетных касс и номер каждой из них (если их число больше одной);</p>

    <p>расписание отправления и прибытия автобусов по рейсам, на которые осуществляется продажа билетов, и тарифы на проезд;</p>

    <p>перечень категорий граждан, которые имеют право на льготы по оплате проезда на автомобильном транспорте и внеочередное приобретение билетов;</p>

    <p>перечень оказываемых пассажирам услуг;</p>

    <p>информация о номерах телефонов, по которым осуществляется бронирование мест для проезда;</p>

    <p>режим работы билетных касс;</p>

    <p>телефоны должностных лиц, к которым могут обратиться пассажиры в случае возникновения спорных ситуаций в процессе их обслуживания.</p>

    <p>210. Продажа билетов может производиться в билетных кассах или в других удобных для пассажиров местах с применением кассовых суммирующих аппаратов транспорта или специальных компьютерных систем.</p>

    <p>211. В камеру хранения пассажирского терминала не должны приниматься вещи, запрещенные к автомобильной перевозке.</p>

    <p>212. Плата за хранение багажа в камере хранения пассажирского терминала взимается в зависимости от размера багажа и на основании почасового тарифа за каждый полный или неполный час, если иное не установлено оператором пассажирского терминала.</p>

    <p>213. Срок хранения багажа в ячейке автоматической камеры хранения пассажирского терминала может быть не более 3 суток, если иное не установлено оператором пассажирского терминала.</p>

    <p>Изъятие багажа из ячейки автоматической камеры хранения пассажирского терминала должно производиться комиссией в составе должностного лица - оператора пассажирского терминала, сотрудника милиции и третьего лица.</p>

    <p>214. Багаж, сданный в камеру хранения пассажирского терминала и не востребованный владельцем в течение 30 дней, подлежит реализации. Его владелец в течение 6 месяцев имеет право получить компенсацию в размере суммы реализации багажа, из которой должны быть вычтены расходы, связанные с хранением и реализацией.</p>

    <p></p>

    <p align="center">Глава 13</p>

    <p align="center">ПРАВА И ОБЯЗАННОСТИ ПАССАЖИРОВ</p>

    <p></p>

    <p>215. Пассажир имеет право:</p>

    <p>приобрести билет на проезд по маршруту, открытому для автомобильных перевозок пассажиров в регулярном сообщении, занять в автобусе место, указанное в билете или автомобильным перевозчиком, либо свободное место и совершить поездку (кроме случаев, когда автомобильный перевозчик может отказать в перевозке);</p>

    <p>пользоваться льготами по оплате проезда на автомобильном транспорте, установленными законами и актами Президента Республики Беларусь;</p>

    <p>бесплатно перевозить одно место маломерной ручной клади и такие личные вещи, как носимая одежда, зонт, телефон мобильной связи, фотоаппарат, портативная видеокамера, трость, а также одну сумку, папку или мешок для предметов личного обихода и документов размерами до 40 x 20 x 10 см или суммой трех измерений не более 70 см и массой не более 5 кг;</p>

    <p>провозить за плату сверх нормы бесплатного провоза (если иное не определено соответствующим договором с автомобильным перевозчиком) в автобусах без багажного отсека не более одного места маломерной ручной клади, а в автобусах с багажным отсеком - не более двух мест маломерного багажа или одного места среднемерного багажа;</p>

    <p>провозить за плату в багажном отсеке (если иное не определено соответствующим договором с автомобильным перевозчиком) при наличии технической возможности и с согласия автомобильного перевозчика сверхнормативное количество маломерных и среднемерных мест багажа, а также крупномерные места багажа;</p>

    <p>провозить бесплатно во внутриреспубликанском сообщении специально обученную собаку-проводника, сопровождающую инвалида по зрению;</p>

    <p>провозить с собой в счет разрешенных к перевозке мест багажа и (или) ручной клади бесплатно или платно (в зависимости от общего числа и мерности мест) мелких домашних животных (собак, кошек и других) и птиц в клетках, ящиках, мешках или сумках с водонепроницаемым адсорбирующим дном при условии ответственности пассажира за соблюдение мер безопасности и санитарных норм;</p>

    <p>провозить в нерегулярном сообщении с согласия автомобильного перевозчика при выполнении условий обеспечения безопасности пассажиров и экипажа собаку крупной породы (высота в холке более 50 см). При этом пассажир должен быть не моложе 16 лет, а если собака потенциально опасной породы, то пассажир должен быть совершеннолетним;</p>

    <p>совершить поездку по приобретенному ранее билету без доплаты, если для автомобильной перевозки пассажиров используется автобус, стоимость проезда в котором выше стоимости приобретенного билета;</p>

    <p>отказаться от поездки с получением уплаченной за нее суммы либо совершить поездку, получив разницу между уплаченной суммой и фактически причитающейся платой за проезд, если автомобильный перевозчик использует автобус, в котором стоимость проезда ниже стоимости приобретенного билета;</p>

    <p>сдать билет до начала поездки и получить в установленном порядке полностью или частично уплаченную стоимость проезда;</p>

    <p>объявлять ценность багажа при его автомобильной перевозке или хранении не выше его реальной стоимости с предъявлением его содержимого;</p>

    <p>обратиться к администрации пассажирского терминала с письменным заявлением о вскрытии ячейки автоматической камеры хранения;</p>

    <p>получать своевременную и точную информацию о расписании движения автобусов, стоимости проезда и других условиях автомобильных перевозок пассажиров;</p>

    <p>пользоваться по собственному выбору дополнительными услугами, предоставляемыми автомобильными перевозчиками и операторами пассажирских терминалов;</p>

    <p>вносить заказчику или оператору автомобильных перевозок пассажиров в регулярном сообщении или автомобильному перевозчику предложения о совершенствовании перевозок, работы пассажирских терминалов, повышении качества обслуживания пассажиров, безопасности автомобильных перевозок, эффективном использовании автобусов;</p>

    <p>требовать от автомобильного перевозчика выполнения условий договора об автомобильной перевозке пассажиров и настоящих Правил;</p>

    <p>получить в соответствии с законодательством возмещение за вред, причиненный его жизни, здоровью или имуществу по вине автомобильного перевозчика.</p>

    <p>216. Пассажир обязан:</p>

    <p>соблюдать требования настоящих Правил, Правил дорожного движения и иных нормативных правовых актов, а также выполнять условия договора об автомобильной перевозке пассажиров, заключенного с автомобильным перевозчиком;</p>

    <p>своевременно приобретать билет и в полном объеме оплачивать проезд, если иное не предусмотрено законами и актами Президента Республики Беларусь, у водителя, в кассе пассажирского терминала или других местах продажи - до начала поездки, у кондуктора (при его наличии) - до следующей остановки;</p>

    <p>оплатить в установленных случаях провоз багажа и (или) ручной клади (иметь багажную квитанцию или подтверждение оплаты провоза ручной клади);</p>

    <p>оплатить услуги, предоставленные в транспортном средстве или на пассажирском терминале;</p>

    <p>иметь и сохранять до конца поездки билеты и багажные квитанции, иметь при себе оригиналы документов, подтверждающих право на льготы по оплате проезда на автомобильном транспорте, и предъявлять их по требованию контролирующих лиц;</p>

    <p>обеспечить сопровождение при городских и пригородных автомобильных перевозках ребенка до 7 лет, при междугородных перевозках - до 14 лет и при международных - до 16 лет;</p>

    <p>производить размещение ручной клади в салоне транспортного средства и места крупномерного багажа в багажном отсеке в соответствии с указаниями автомобильного перевозчика;</p>

    <p>по прибытии в пункт назначения освободить салон транспортного средства и получить багаж;</p>

    <p>осуществлять посадку в транспортное средство и высадку из него в соответствии с требованиями настоящих Правил;</p>

    <p>занимать место, указанное в билете;</p>

    <p>уступать при городской перевозке пассажиров в регулярном сообщении специально обозначенные места для сидения пассажирам с детьми дошкольного возраста, беременным женщинам, инвалидам и лицам пожилого возраста;</p>

    <p>держаться за поручень во время движения автобуса при проезде в салоне автобуса стоя;</p>

    <p>ожидать транспортное средство на посадочной площадке, а при ее отсутствии - на тротуаре; после выхода из транспортного средства не находиться на краю посадочной площадки или на краю тротуара;</p>

    <p>выходить на проезжую часть, если посадка с посадочной площадки или тротуара невозможна, только после остановки транспортного средства;</p>

    <p>заблаговременно известить водителя о необходимости остановки автобуса при наличии на маршруте остановки по требованию;</p>

    <p>не причинять своими действиями вред пассажирам, ущерб их багажу и ручной клади, а также салону и багажному отсеку транспортного средства;</p>

    <p>сообщать водителю об обнаруженных в салоне транспортного средства вещах и документах, оставленных без присмотра;</p>

    <p>при автомобильной перевозке собаки или кошки обеспечить наличие жетона и регистрационного свидетельства животного с отметкой ветеринарного врача о вакцинации против бешенства, а в установленных случаях - ветеринарного свидетельства или ветеринарной справки;</p>

    <p>соблюдать санитарные нормы при автомобильной перевозке домашних животных (собак, кошек и других) и птиц;</p>

    <p>иметь надлежащим образом оформленные документы на право пересечения границ соответствующих государств при осуществлении международной поездки;</p>

    <p>соблюдать установленные правила пересечения границы государства, по территории которого выполняется международная автомобильная перевозка пассажиров, присутствовать во время таможенного осмотра багажа и другого его имущества, а также при отборе контролирующими органами для экспресс-анализа образцов и проб принадлежащих ему веществ и материалов;</p>

    <p>возместить в установленном законодательством порядке автомобильному перевозчику и (или) другим пассажирам ущерб, причиненный собственными неосторожными или умышленными действиями;</p>

    <p>сохранять отрывную часть багажной бирки в течение всей поездки и предъявлять ее водителю для получения багажа;</p>

    <p>при утере багажной квитанции, отрывной части багажной бирки или документа на хранение вещей в камере хранения подать лицу, ответственному за вещи, письменное заявление с указанием вида упаковки и описи вещей;</p>

    <p>не создавать препятствий для передвижения пассажиров по салону автобуса при размещении ручной клади в проходах и на площадках у дверей автобуса, а также не размещать ручную кладь на сиденьях транспортных средств;</p>

    <p>не создавать своим состоянием, поведением и действиями проблем другим пассажирам, водителям и кондукторам;</p>

    <p>не пользоваться аварийным оборудованием транспортного средства в ситуации, не угрожающей жизни и здоровью людей;</p>

    <p>не засорять салон транспортного средства;</p>

    <p>не провозить запрещенные к перевозке предметы в багаже и (или) ручной клади;</p>

    <p>не провозить в салоне (кроме автомобиля-такси) места среднемерной (при наличии багажного отсека) и крупномерной ручной клади;</p>

    <p>не курить в салоне транспортного средства;</p>

    <p>не находиться в одежде или с ручной кладью, загрязняющей одежду пассажиров или салон транспортного средства.</p>

    <p>217. Пассажир несет ответственность за нарушение требований настоящих Правил, нанесение материального ущерба автомобильному перевозчику, заказчику или другим участникам автомобильной перевозки пассажиров.</p>

