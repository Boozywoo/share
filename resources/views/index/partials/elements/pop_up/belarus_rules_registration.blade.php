    <p align="center">Глава 5</p>

    <p align="center">ПОРЯДОК ВЫПОЛНЕНИЯ АВТОМОБИЛЬНОЙ ПЕРЕВОЗКИ БАГАЖА</p>

    <p align="center">И РУЧНОЙ КЛАДИ</p>

    <p></p>

    <p>63. Багаж к автомобильной перевозке принимается только от лиц, имеющих билет на проезд в данном автобусе и багажную квитанцию, с использованием багажной бирки.</p>

    <p>64. Багажная бирка должна изготавливаться типографским способом, состоять из двух частей, иметь перфорацию для облегчения отрыва одной части от другой, а также одинаковый номер на каждой ее части.</p>

    <p>65. При приеме багажа к автомобильной перевозке автомобильный перевозчик должен выдать пассажиру на каждое место багажа, размещенного в багажном отсеке, часть багажной бирки, а другую часть прикрепить к этому месту багажа. Багаж считается принятым к перевозке с момента вручения пассажиру части багажной бирки, а выданным - с момента выдачи багажа пассажиру, предъявившему представителю автомобильного перевозчика багажную квитанцию и выданную ему при приеме багажа часть багажной бирки.</p>

    <p>После выдачи багажа предъявленная пассажиром часть багажной бирки остается у водителя. Выдача пассажиру багажа без претензий со стороны пассажира предполагает (если не доказано обратное), что багаж был выдан полностью и в неповрежденном состоянии. Претензия по возникшим во время перевозки скрытым дефектам багажа может быть предъявлена автомобильному перевозчику не позднее одних суток с момента выдачи багажа пассажиру.</p>

    <p>66. Если во время выдачи багажа пассажиру обнаружена недостача, повреждение или утрата багажа, на багажной квитанции должна быть сделана соответствующая отметка и составлен акт в порядке, предусмотренном в пункте 252 настоящих Правил.</p>

    <p>67. Запрещается перевозить багаж и ручную кладь, содержащие взрывоопасные, огнеопасные, окисляющие, коррозионные, радиоактивные, отравляющие, ядовитые (токсичные), озоноразрушающие, наркотические, психотропные, зловонные, с резким запахом вещества и предметы, острые и (или) режущие предметы (без упаковки или чехлов, исключающих причинение вреда пассажирам и ущерба их имуществу), огнестрельное оружие без наличия у пассажира документа, подтверждающего право на его ношение, и не вложенное в чехол (кобуру), а также другие предметы и вещества, которые при перевозке могут причинить вред пассажирам, водителю, кондуктору или вызвать повреждение или загрязнение транспортного средства, багажа и (или) ручной клади. К перевозке не принимаются также хрупкие предметы, деньги, ювелирные изделия, драгоценные металлы, камни и изделия из них, ценные и деловые бумаги, ключи, документы, удостоверяющие личность, документы для выезда из Республики Беларусь и (или) въезда в Республику Беларусь, удостоверения и иные документы, подтверждающие специальный статус (права и обязанности) их владельца, другие подобные предметы. Места багажа принимаются только в исправной закрытой упаковке.</p>

    <p>(в ред. постановления Совмина от 23.12.2008 N 2010)</p>

    <p>(см. текст в предыдущей редакции)</p>

    <p>68. Плата за провоз багажа и (или) ручной клади производится пассажиром до начала поездки (при городских автомобильных перевозках в регулярном сообщении после посадки не позже момента прибытия на следующий остановочный пункт). Оплата производится, если иное не предусмотрено договором об автомобильной перевозке пассажира, по установленному тарифу за каждое среднемерное место багажа и (или) ручной клади, в двукратном размере установленного тарифа за перевозку каждого крупномерного места багажа и в половинном размере установленного тарифа за каждое маломерное место багажа и (или) ручной клади, перевозимой сверх нормы бесплатного провоза.</p>

    <p>69. Багажная квитанция должна содержать:</p>

    <p>номер;</p>

    <p>наименование автомобильного перевозчика или оператора;</p>

    <p>стоимость перевозки багажа;</p>

    <p>информацию о числе мест и их мерности, об объявленной ценности;</p>

    <p>отметки о приеме и выдаче багажа автомобильным перевозчиком.</p>

    <p>70. Багажная квитанция без предъявления билета на поездку пассажира недействительна.</p>

    <p>71. Если пассажир, сдавая багаж к автомобильной перевозке, объявил его ценность, то в багажной квитанции автомобильным перевозчиком или его представителем делается соответствующая отметка. Объявленная ценность багажа не может быть выше его реальной стоимости.</p>

    <p>72. Багаж, не востребованный пассажиром или иным лицом, уполномоченным на получение багажа по прибытии автобуса в пункт назначения, или оставленные пассажиром в ходе перевозки багаж и (или) ручная кладь независимо от того, была ли выдана багажная квитанция, хранятся автомобильным перевозчиком, за что с пассажира при получении багажа и (или) ручной клади взимается плата. Автомобильный перевозчик может поручить хранение багажа или ручной клади третьему лицу на основании договора хранения.</p>

    <p>73. Невостребованный пассажиром багаж или оставленная ручная кладь хранятся автомобильным перевозчиком в течение 30 дней с даты прибытия автобуса на конечный пункт следования. Скоропортящиеся продукты, находящиеся в багаже или ручной клади и имеющие признаки порчи, уничтожаются с составлением комиссионного акта. По истечении 30 дней хранения данный багаж или ручная кладь могут быть реализованы автомобильным перевозчиком после проведения комиссионной оценки их стоимости. Багаж или ручная кладь, не имеющие ценности, могут быть уничтожены с составлением комиссионного акта. Пассажир или иное лицо, уполномоченное на получение багажа, в течение шести месяцев с даты реализации невостребованного багажа имеет право получить сумму, вырученную автомобильным перевозчиком от его реализации, за вычетом расходов, связанных с хранением и реализацией такого багажа.</p>

    <p>74. Багаж (его часть), не выданный пассажиру в течение 14 суток со дня поступления от пассажира заявления о выдаче багажа, считается утерянным. В таком случае пассажиру возмещается ущерб в порядке, установленном в пункте 241 настоящих Правил.</p>

    <p>75. Если у пассажира отсутствуют багажная квитанция и (или) багажная бирка, то автомобильный перевозчик вправе выдать багаж на основании соответствующего заявления пассажира в случае предъявления им достаточных доказательств, свидетельствующих о праве на этот багаж, с оформлением акта выдачи, содержащего данные документа, удостоверяющего личность, или свидетельства о регистрации ходатайства о предоставлении статуса беженца или дополнительной защиты в Республике Беларусь либо свидетельства о предоставлении дополнительной защиты в Республике Беларусь (в случае отсутствия документа, удостоверяющего личность) пассажира и опись передаваемых вещей. Если доказательства, свидетельствующие о праве на багаж, представляются автомобильному перевозчику недостаточными, он вправе потребовать от пассажира внесения залога в соответствии с пунктом 241 настоящих Правил.</p>

    <p>(в ред. постановлений Совмина от 23.12.2008 N 2010, от 19.05.2009 N 646)</p>

    <p>(см. текст в предыдущей редакции)</p>

    <p>Залог подлежит возврату по истечении трех месяцев со дня выдачи багажа в случае отсутствия заявления о выдаче багажа другими лицами.</p>

    <p>76. Если багаж, считающийся утерянным, найден в течение года со дня подачи пассажиром заявления о выдаче багажа, автомобильный перевозчик должен уведомить об этом пассажира. В течение 30 дней со дня такого уведомления пассажир может получить этот багаж, возвратив автомобильному перевозчику денежные средства, которые он получил в виде компенсации за утрату багажа.</p>

    <p>77. Если пассажир отменяет оплаченную перевозку багажа до ее начала или перевозка багажа не состоялась по вине автомобильного перевозчика, пассажиру должна быть возвращена плата за провоз багажа.</p>

    <p>78. Выдача невостребованного пассажиром багажа или оставленной им ручной клади должна осуществляться на основании его письменного заявления и соответствующего акта, в который заносятся данные документа, удостоверяющего личность, или свидетельства о регистрации ходатайства о предоставлении статуса беженца или дополнительной защиты в Республике Беларусь либо свидетельства о предоставлении дополнительной защиты в Республике Беларусь (в случае отсутствия документа, удостоверяющего личность) пассажира и информация о фактах, доказывающих, что эти вещи ему принадлежат. Расходы, связанные с пересылкой и хранением оставленных вещей, возмещаются за счет пассажира.</p>

    <p>(в ред. постановлений Совмина от 23.12.2008 N 2010, от 19.05.2009 N 646)</p>

