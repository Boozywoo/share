    <p align="center">Глава 3</p>

    <p align="center">ТРЕБОВАНИЯ К ВОДИТЕЛЯМ ТРАНСПОРТНЫХ СРЕДСТВ</p>

    <p></p>

    <p>19. К выполнению городской и пригородной автомобильных перевозок пассажиров автобусами допускаются водители не моложе 21 года, имеющие водительское удостоверение на право управления транспортными средствами соответствующей категории - "D" или "D" и "E".</p>

    <p>20. К выполнению междугородных автомобильных перевозок пассажиров автобусами категорий М2 и М3 допускаются водители, имеющие общий стаж работы не менее двух лет в качестве водителя транспортных средств категории "D".</p>

    <p>21. К выполнению международных автомобильных перевозок пассажиров автобусами категории М2 допускаются водители, имеющие общий стаж работы не менее двух лет в качестве водителя транспортных средств категории "D".</p>

    <p>22. К выполнению международных автомобильных перевозок пассажиров автобусами категории М3 допускаются водители, имеющие общий стаж работы водителем автобуса категории М3 не менее трех лет.</p>

    <p>23. К выполнению автомобильных перевозок пассажиров автомобилями-такси допускаются водители не моложе 20 лет, имеющие водительское удостоверение на право управления транспортными средствами категории "B" и общий стаж управления автомобилем не менее двух лет или стаж работы в качестве водителя автомобиля не менее одного года.</p>

    <p>24. Водители должны знать требования нормативных правовых актов, регламентирующих выполняемые ими автомобильные перевозки пассажиров. Водители, выполняющие автомобильные перевозки пассажиров автомобилями-такси и международные перевозки пассажиров, должны пройти аттестацию по соответствующим программам профессиональной подготовки, утверждаемым Министерством транспорта и коммуникаций.</p>

    <p>25. Стаж работы в качестве водителя автомобиля подтверждается при приеме его на работу записями в трудовой книжке, а при необходимости - справками с предыдущих мест работы, заверенными копиями приказов или трудовых договоров (контрактов).</p>

    <p>Стаж управления автомобилем подтверждается водительским удостоверением и документами, удостоверяющими владение и пользование автомобилями.</p>

    <p>26. Допуск водителя транспортного средства к самостоятельной работе производится автомобильным перевозчиком после проведения стажировки водителя в порядке, установленном Министерством транспорта и коммуникаций.</p>

    <p>27. Водитель допускается автомобильным перевозчиком к выполнению перевозок пассажиров после прохождения в установленном порядке обязательного медицинского переосвидетельствования водителей механических транспортных средств, а также предрейсового медицинского обследования перед началом работы, о чем должна быть произведена отметка в путевом листе. Медицинское обследование водителя может проводиться дополнительно во время работы и (или) после ее окончания.</p>

    <p>28. Водитель, выполняющий автомобильные перевозки пассажиров, должен иметь и представлять для проверки по требованию контролирующих лиц следующие документы:</p>

    <p>водительское удостоверение и талон к нему на право управления транспортным средством соответствующей категории;</p>

    <p>карточку водителя и (или) регистрационные листки (тахограммы, распечатки) в необходимом количестве, если транспортное средство оборудовано контрольным устройством (тахографом);</p>

    <p>свидетельство о регистрации транспортного средства (технический паспорт автомобиля);</p>

    <p>документ (сертификат), подтверждающий прохождение государственного технического осмотра;</p>

    <p>страховое свидетельство (страховой полис, страховой сертификат), подтверждающее заключение договора обязательного страхования гражданской ответственности владельцев транспортных средств;</p>

    <p>страховое свидетельство (страховой полис) либо заверенную автомобильным перевозчиком его копию, подтверждающее заключение договора обязательного страхования гражданской ответственности автомобильного перевозчика перед пассажирами;</p>

    <p>путевой лист установленной формы;</p>

    <p>лицензионную карточку на данное транспортное средство;</p>

