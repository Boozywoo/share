    <p>свидетельство о прохождении аттестации в соответствии с пунктом 24 настоящих Правил (при выполнении международных перевозок и перевозок автомобилями-такси);</p>

    <p>удостоверение о прохождении водителем обучения безопасной работе с газобаллонными транспортными средствами и акты о прохождении освидетельствования баллонов и о переоборудовании транспортного средства для работы на газовом топливе (при выполнении перевозок на транспортном средстве, работающем на сжатом или сжиженном газе).</p>

    <p>При выполнении городских, пригородных или междугородных автомобильных перевозок пассажиров в регулярном сообщении водитель дополнительно должен иметь:</p>

    <p>карточку маршрута автомобильных перевозок пассажиров в регулярном сообщении (далее - карточка маршрута) по форме, утверждаемой Министерством транспорта и коммуникаций;</p>

    <p>копию схемы маршрута;</p>

    <p>расписание движения автобуса.</p>

