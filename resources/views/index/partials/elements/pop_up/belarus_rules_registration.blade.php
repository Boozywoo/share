    <p>29. Основные условия выполнения автомобильных перевозок пассажиров определяются Законом Республики Беларусь "Об автомобильном транспорте и автомобильных перевозках".</p>

    <p>Систематические автомобильные перевозки пассажиров, которые выполняются в одно и то же время или с устоявшимися интервалами движения по одному и тому же маршруту по обращению любого физического лица, должны осуществляться как перевозки пассажиров в регулярном сообщении.</p>

    <p>Автомобильные перевозки пассажиров в регулярном сообщении выполняются по договору об автомобильной перевозке пассажиров, а в нерегулярном сообщении - по договору о фрахтовании для автомобильной перевозки пассажиров или по договору о перевозке пассажиров автомобилем-такси.</p>

    <p>По договору об автомобильной перевозке пассажиров автомобильный перевозчик обязуется перевезти пассажира в пункт назначения, а в случае сдачи пассажиром багажа также доставить багаж в пункт назначения и выдать его пассажиру либо уполномоченному им на получение багажа лицу. Пассажир, если иное не предусмотрено законами и актами Президента Республики Беларусь, обязан заплатить установленную плату за проезд, а при сдаче багажа - и за провоз багажа. Заключение договора об автомобильной перевозке пассажира удостоверяется билетом или иным документом, предусмотренным законодательством, а сдача пассажиром багажа - багажной квитанцией.</p>

